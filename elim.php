    <p class="indent"><strong>Bennebroek, benedictinessen: Muller, <i>Gesch.
    Atlas </i>I, 304</strong>. Dit klooster zou een dochter van de
    abdij Egmond zijn, en in de Haarlemmermeer zijn verdronken. Muller beroept zich
    op De la Torre, <i>AAU</i> 11 (1883) 126. De veronderstelling van een
    benedictinessenklooster te Bennebroek berust uiteindelijk op de mededeling in
    de <i>Tabula</i>, een veertiende-eeuwse Egmondse geschiedbron<i> </i>(Egmondsche
    Annalen in <i>BMHG </i>XXXV 49; Oppermann, <i>Fontes Egmundenses </i>(1933)
    105; Carasso-Kok nr. 367; NarrSource NL0538) dat graaf Dirk II de monialen die
    hij weghaalde uit het oorspronkelijke klooster te Egmond om plaats te maken
    voor benedictijner monniken, in ‘Bennincbroec’ vestigde. Het zal hierbij
    inderdaad om Bennebroek in Kennemerland gaan, niet om het Westfriese
    Benningbroek, want de monialen moesten juist wijken voor het Friese gevaar.
    Oudere bronnen, van Rupert van Mettlach (CK nr. 1; NarrSource NL0001; Vis en
    Van Rij (1987) 57) tot en met Beka (CK nr. 296; NarrSource J189; ed. Bruch p.
    61) vermelden wel de verwijdering van de monialen (E17) uit Egmond, maar geven
    niet aan wat er daarna met hen gebeurde. De <i>Tabula </i>heeft gebruik gemaakt
    van oudere, nu verloren bronnen - zie Gumbert in Vis (2008) 69 – zodat de
    mededeling niet zonder meer kan worden afgewezen. Maar in ieder geval volgden
    deze monialen, anders dan de monniken die hen vervingen, niet de regel van
    Benedictus. De verdrinking in het Haarlemmermeer is een veronderstelling.</p>



