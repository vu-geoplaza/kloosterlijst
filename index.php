    <p class="indent">Sinds de herziening van winter 2014/5 worden foto’s toegevoegd aan de beschrijving van die
        kloosters waarbij nog van enigerlei herkenbaarheid in het openbare domein sprake is. Plaatsen van herinnering
        aan onze middeleeuwse kloosters zijn er in verschillende soorten. Slechts bij een kleine minderheid van de
        kloosters staat nog een substantieel deel van de gebouwen overeind. De Abdij van Middelburg (M28) spant hierbij
        de kroon, ondanks de ingrijpende restauratie die nodig was na het bombardement van 1940. Het andere uiterste
        wordt gevormd door die kloosters waarvan alleen – al dan niet opgemetselde – fundamenten over zijn, zoals het
        Sint-Jansklooster (tertianen van Sint-Janskamp) in het Land van Vollenhove (V14). Vaker dan gedacht gaan in
        monumentale gebouwen uit de Nieuwe Tijd bouwdelen schuil die tot de Middeleeuwen teruggaan. Voorbeelden zijn het
        Weeshuis aan de Spieringstraat in Gouda, dat resten van het Margarethaklooster van regularissen bevat (G13) en
        het Rode Weeshuis aan de gelijknamige straat in Groningen, waarin onderdelen van het Oldeconvent (G41) zijn
        bewaard. De beste kans op overleven hadden de kloosterkapellen, waarvoor vaak een andere bestemming te vinden
        was. Een voorbeeld is de Agnietenkapel in Amsterdam (A26), die als Universiteitsmuseum dienst doet. Ook bij
        dergelijke kapellen is de oorspronkelijke vorm vaak moeilijk herkenbaar, als gevolg van functionele aanpassing
        zoals bij de Begijnhofkerk in Haarlem (H03) of als resultaat van een ingrijpende restauratie, zoals bij de
        Goudse Agnietenkapel (G20). Een curieus overblijfsel is de waterput van het Agnesconvent van tertiarissen in
        Oldenzaal (O08), die werd ontdekt naar aanleiding van de bouw van een parkeergarage en die daarin nu is
        opgenomen. Soms zijn zichtbare overblijfselen te vinden op een andere plek dan men zou verwachten. Het convent
        van de tertiarissen van Sint-Catharina in Almelo (A15) heeft in die plaats zelf geen sporen nagelaten. Maar toen
        de religieuzen vandaar in 1667 werden verdreven, herstichtten zij hun klooster als Maria Vlucht op de Glaan
        juist over de Duitse grens: die plek kan nog worden bezichtigd. Een ander klooster dat voortbestond tot diep in
        de tijd van de Republiek, is het regularissenklooster Nazareth te Waalwijk (W01). Daarvan is een barok
        toegangspoortje over, dat nu is ingebouwd in de toren van de imposante parochiekerk van Sint-Jan de Doper (die
        overigens vrijwel op dezelfde plaats staat).</p>

    <p class="indent">Vaak is het alleen een straatnaam die aan een voormalig klooster herinnert. Voorbeelden van een
        ‘Bagijnenstraat’ of ‘Kloosterweg’ zijn legio. Origineler is het Amsterdamse ‘Gebed zonder End’, een doodlopend
        straatje dat de herinnering aan de tertiarissen van Sint-Clara (A27) bewaart. Soms wordt niet het klooster zelf
        herdacht, maar een markante kloosteroverste, zoals priorin Machteld van Meteren bij de Delftse norbertinessen
        van Koningsveld (D02). Een enkele keer is de naam van een klooster verbasterd in een straat- of streeknaam
        terechtgekomen maar toch welbekend: Bartlehiem (O23). Een recente trend is het levendig houden van de
        herinnering aan een klooster door er een modern opvolgergebouw naar te vernoemen, zoals de Agnietenburgh in
        Kampen (K07); hoewel dit ook vroeger al wel voorkwam, zoals het toegangspoortje van het woonzorgcentrum de
        Beyart in Maastricht (M20) bewijst. Niet zelden zijn het boerderijnamen waarin een klooster voortleeft; een
        voorbeeld is de Waterlooze Werve op Walcheren, dat naar het voormalige klooster van cisterciënzerinnen (A01)
        verwijst.</p>

    <p class="indent">Van een andere categorie zijn de informatiepanelen die op veel plaatsen – vaak onder auspiciën van
        de ANWB – zijn aangebracht. Een voorbeeld betreft de norbertijnen van Kuzemer (O05). De informatie op dergelijke
        panelen is wel eens misleidend: zo worden de bewoonsters van het Haarlemse tertiarissenconvent Sint-Ursula als
        ‘Ursulinen’ aangeduid (H08).</p>

    <p class="indent">Enkele bijzondere plaatsen van herinnering zijn in de meest recente tijd ingericht. Bij de
        parochiekerk in Albergen plaatste de Werkgroep St. Antoniusklooster een fraaie bronzen maquette, met toelichting
        ook in brailleschrift, ter herinnering aan het klooster van regulieren (O15). Opgravingen op het terrein van het
        regulierenklooster Nieuwlicht te Westerblokker (W20) leidden tot het opmetselen van een poortje waarin een
        teruggevonden sluitsteen werd verwerkt. Eveneens in Westerblokker werden de stoffelijke resten die bij de
        opgraving van het Bethlehemklooster van regularissen (W21) te voorschijn kwamen, plechtig herbegraven in een
        tombe bij de dorpskerk die wordt afgedekt door een beeldengroep. Hier en daar houdt een modern kunstwerk de
        herinnering aan een voormalig klooster levendig, zoals in Deventer (D25) en in Scharnegoutum (S03).</p>

    <p class="indent">Dit overzicht eindigt met drie speciale gevallen. Het klooster van de regulieren van Hemsdonk in
        de Alblasserwaard (B29) werd gesticht op een donk (rivierduin) in het veenlandschap. Het klooster is verdwenen,
        maar de donk is nog altijd goed te zien. Aan het regulierenklooster Mariënborn of –daal onder Oosterbeek (O11)
        herinnert niet alleen de naam van het landgoed dat daar nu ligt, maar ook de grafsteen die als tafelblad dienst
        doet ten gerieve van picknickgasten: De Stenen Tafel. De regularissen van Renkum (R07) hadden hun klooster bij
        een bedevaartskapel ter ere van Maria. Over hun terrein loopt nu een provinciale weg, maar het beeldje uit de
        kapel kan nog altijd worden vereerd, in de twintigste-eeuwse kerk van OLV Tenhemelopneming in het dorp
        Renkum.</p>

    <a name="kk"><h3 class="indent">Kloosterkaart</h3></a>

    <p class="indent">Na de publicatie in 2016 van een proefversie die de situatie van circa 1500 weergaf, is de
        Kloosterkaart in zijn huidige vorm in het voorjaar van 2017 op het web gezet. Kenmerkend daarvoor is zijn
        interactief-diachrone karakter, dat wil zeggen dat gezocht kan worden met een glijdende tijdsschaal. De
        Kloosterkaart is vervaardigd onder regie van de Universiteitsbibliotheek van de VU in de persoon van de
        conservator Kaarten en Atlassen Lida Ruitinga; ontwerp en programmering zijn het werk van Peter Vos. De kaart is
        geplaatst op Geoplaza, de door UBVU onderhouden portal voor GIS-gerelateerde bestanden. De records uit de
        Kloosterlijst kunnen vanuit de Kloosterkaart direct worden opgeroepen.</p>

    <p class="indent">De lancering van een nieuwe versie van de Kloosterlijst in 2019 brengt ook een aanpassing van de
        Kloosterkaart met zich mee. De tijdsdimensie is uitgebreid en omvat nu ook het kloosterleven van de zeventiende
        en achttiende eeuw, zowel dat van de kloosters uit de Middeleeuwen die werden gecontinueerd, als dat van de
        kloosters die in de Nieuwe Tijd nieuw zijn ontstaan. Opvallend is het grote aantal (vaak gedwongen)
        verplaatsingen. Daarnaast zijn twee subsets van gegevens uit de Kloosterlijst voor het eerst in de Kloosterkaart
        verwerkt, die met betrekking tot de Kapittels en tot de Uithoven. De gegevens voor de kapittels zijn diachroon,
        op dezelfde voet als die voor de kloosters. Voor de uithoven zijn de gegevens in het algemeen te schaars om ze
        in de tijd te kunnen plaatsen; daarom worden ze synchroon benaderd. De kaart toont alleen de uithoven bij de
        kloosters die op dat moment zijn geselecteerd. Het is ook mogelijk om het verband tussen individuele kloosters
        en de bijbehorende uithoven zichtbaar te maken.</p>

    <p class="indent">Deze Kloosterkaart komt in de plaats van de kloosterkaart die door S. Muller Hzn e.a. in 1921-1923
        is gepubliceerd in de <em>Geschiedkundige Atlas van Nederland</em> van A.A. Beekman. Het <em>Monasticon
            Batavum</em> (1941-1942) was niet van een begeleidende kaart voorzien. Sindsdien heeft het onderzoek niet
        stilgestaan: de belangrijkste publicaties die per klooster worden genoemd in de Kloosterlijst zijn ook zo goed
        mogelijk verwerkt in deze Kloosterkaart. Er is verder systematisch rekening gehouden met de stadsplattegronden
        van Jacob van Deventer uit circa 1560 (moderne edities door Koeman, Visser en Van der Krogt, 1992-2001; en Rutte
        en Vannieuwenhuyze, 2018; zie ook de website van de <a
                href="http://bdh.bne.es/bnesearch/q/Jacob%20van%20Deventer" target="_blank">Biblioteca Nacional de
            España</a>). Voor de noordelijke provincies, in het bijzonder Friesland, kon worden teruggevallen op de <a
                href="http://www.hisgis.nl/hisgis/gewesten/fryslan/Stand%20van%20zaken%20en%20programma"
                target="_blank"> HISGIS site</a> van de Fryske Akademy in Leeuwarden. De kaart ‘Kloosters omstreeks
        1300’ in <em>De Bosatlas van de Geschiedenis van Nederland</em> (p. 119) berust op de gegevens in De Nijs en
        Kroeze (2008-2011), waarin het recente onderzoek niet systematisch is verwerkt.</p>

    <p class="indent">Ten behoeve van de plaatsbepaling was in de Kloosterlijst al een veld gereserveerd voor (globale)
        coördinaten volgens het Rijksdriehoeksstelsel, zoals raadpleegbaar in de <em>Grote
            Topografische Atlas van Nederland 1:50.000</em>. Het Kadaster hanteert het stelsel van de
        Rijksdriehoekmeting als coördinatenstelsel van Nederland. Het wordt ook toegepast op de Archeologische
        Monumentenkaart. Dit veld is nu geplaatst bij het blokje met velden voor de archeologische gegevens. Voor de
        plaatsing van de kloosters, kapittels en uithoven op de Kloosterkaart is gebruik gemaakt van de geografische
        coördinaten die ontleend zijn aan de internationaal geldende graden Noorderbreedte en Oosterlengte. De records
        voor de kapittels en de uithoven bevatten velden voor de graden Noorderbreedte en Oosterlengte.
    </p>

    <p class="indent">De items zijn als punten weergegeven, niet als vlakken. De reden is dat slechts bij een minderheid
        van de instellingen de omtrek van het kloosterterrein enigermate nauwkeurig bekend is; en dan nog zou een keuze
        voor vlakken op dit moment te tijdrovend zijn geweest. Indien bekend, is de plek van de kapel aangehouden. Als
        ondergrond van de kaart kan gekozen worden tussen de OpenStreetMap in een standaard- en een gestileerde versie,
        Bing Road (een topografische kaart van Bing Maps) en Bing Aerial. Deze laatste geeft een satelliet-luchtfoto als
        achtergrond, waarop de huidige bebouwing goed te zien is.</p>

    <p class="indent">-indien
        de exacte locatie van een klooster onbekend is, dan is het geplaatst bij de
        parochiekerk (dit is met name bij veel vroege begijnhuizen het geval)</p>

    <p class="indent">Iconen geven de verschillende orden aan waartoe de kloosters en religieuze huizen hebben behoord.
        Ze zijn gegroepeerd in enkele grote categorieën, die elk een eigen kleur hebben: regel van Benedictus, regel van
        Augustinus, regel(s) van Franciscus, geestelijke ridderorden, overige regels, en religieuze huizen zonder
        erkende regel. De plaatsing van het kruisje boven en/of onder het icoon geeft aan of het een mannen-, een
        vrouwen- of een dubbelklooster betreft. Verandert een klooster tussentijds van orde, dan wordt dit ook zichtbaar
        in de wisseling van het icoon. Kapittels worden aangeduid door het icoon voor de seculiere kanunniken met een
        extra rode markering. Voor uithoven wordt een uniform symbool gebruikt; de koppeling aan het desbetreffende
        klooster wordt zichtbaar door omcirkeling.</p>

    <p class="indent">Het is belangrijk te bedenken dat bij het projecteren van de gegevens uit de Kloosterlijst op de
        kaart nuances kwamen te vervallen en voor onzekerheden een oplossing moest worden bedacht. Daarom zijn een
        aantal <strong>conventies</strong> toegepast:<br>
        -indien de exacte locatie van een klooster onbekend is, dan is het geplaatst bij de parochiekerk (dit is met
        name bij veel vroege begijnhuizen het geval)<br>
        -uithoven konden dikwijls slechts bij benadering worden gesitueerd; er is dan gekozen voor een snijpunt op de
        <em>Topografische Atlas 1:50.000</em><br>
        -indien bij kloosters met tussentijdse veranderingen de datum van de eerste fase niet bekend is, dan wordt deze
        geplaatst één jaar voor het eerst bekende jaartal. Zie bv U03 (Oostbroek): 1113 -> 1112<br>
        -indien een stadium exact wordt gedateerd maar met een meerjarenperiode (type 1402-1405*: de stichtingsfase
        beslaat de jaren 1402-1405) wordt voor de kaart het laatste jaar gehanteerd<br>
        -een datering van het type ‘1450* of 1454*’ (bv W01, Waalwijk): het laatste jaar, dus 1454<br>
        -dateringen van het type ‘ca. 1405’ of ‘1405?’: de modificatie wordt weggelaten<br>
        -type ‘tussen 1397 en 1410’: het laatste jaar wordt gehanteerd<br>
        -type ‘kort na 1400’: 1401<br>
        -‘14e eeuw’: 1350<br>
        - tertiarissenconventen zijn vaak voortgekomen uit zusterhuizen des gemenen levens, zonder dat het tijdstip
        waarop dat gebeurde bekend is. Niet zelden is de lijst van 1555 – zie Van Heel (1936) - de eerste attestatie van
        de nieuwe status (bv W16: Jonge Hof te Weesp). Analogie met vergelijkbare tertiarissenconventen leert dat zo’n
        overgang toch vermoedelijk al in de vijftiende eeuw heeft plaatsgevonden. Er is dan een conventioneel jaar
        ingevoerd, zoals 1450 of 1475<br>
        -in de tijd van de Opstand vinden veel fusies van kloosters plaats, waarbij de inwoners van het ene naar het
        andere klooster verhuizen. In de verbale velden van de Kloosterlijst staat dan zowel bij de ontvangende als bij
        de ontvangen gemeenschap een notitie. De kaart gaat alleen door met het ontvangende klooster.
    </p>

    <a name="kho"><h3 class="indent">Kort historisch overzicht</h3></a>

    <p class="indent">Ter ori&euml;ntatie volgt hier een beknopt overzicht van de historische ontwikkeling van het
        kloosterwezen in Nederland. Vergeleken met de omringende landen is dit in het huidige Nederland laat tot
        ontwikkeling gekomen. Wel hebben ‘buitenlandse’ abdijen hier al vroeg bezittingen gehad, die soms als hoven
        waren georganiseerd. Maar het aantal kloosters dat zich voor 1100 in ons land zelf vestigde, is zeer gering. In
        de Karolingische tijd ontstond differentiatie tussen kloosters met de regel van Benedictus en seculiere
        kapittels die de regel van Aken volgden.</p>

    <p class="indent">In de twaalfde en dertiende eeuw kwamen de nieuwe plattelandsorden tot ontwikkeling, met mannen-
        en vrouwenkloosters – en vooral in de begintijd een aanzienlijk aantal dubbelkloosters – van reguliere
        kanunniken,
        cisterci&euml;nzers en norbertijnen. De noordelijke provincies en het zuidwesten namen hierbij het voortouw.
        Deze kloosters hadden – wat de koormonniken en -nonnen betreft – een adellijk karakter. Maar ze telden ook grote
        aantallen conversen (lekebroeders), die de landerijen bewerkten; het grondbezit was veelal georganiseerd in
        uithoven. Kenmerkend met name voor de norbertijnen en de reguliere kanunniken is dat zij taken in de zielzorg
        binnen de plattelandsparochies op zich namen. In deze periode vestigden zich ook de geestelijke ridderorden in
        ons land, waarbij de Duitse Orde in het algemeen een voorsprong had op de johannieters. Aan het vroege jaartal
        dat voor de johannieter commanderij in Utrecht wordt vermeld (1122), mag getwijfeld worden. De Duitse Orde telde
        in ons land naast priesters ook ridders met een adellijke achtergrond. In de commanderijen van de johannieters
        woonden alleen priesters. Het aantal nederzettingen van tempeliers bleef beperkt tot één, hooguit twee (Alphen;
        A60 en Zaamslag; Z39), maar dat wordt ruimschoots gecompenseerd door de wonderbare tempelierenvermenigvuldiging
        in de latere legende (zie de eliminatielijst).</p>


    <p class="indent">Volledigheidshalve volgt hier een opsomming van incidentele vestigingen in de dertiende eeuw: de
        magdalenen, een orde van gevallen vrouwen, waarvan de kloosters spoedig naar andere orden zijn overgegaan; de
        wilhelmieten, een orde met een eremitische (kluizenaars-) inslag, gesticht door Willem van Malaville; de
        antonieten, oorspronkelijk een broederschap met een verpleegtaak in verband met het antoniusvuur en
        georganiseerd naar analogie van de ridderorden; de victorinnen, reguliere kanunnikessen gelieerd aan het
        hervormingsklooster Sint-Victor bij Parijs; en de aan de kartuizers verwante caulieten, genoemd naar de
        oorspronkelijk vestiging Val-des-Choux in Bourgondi&euml;. </p>

    <p class="indent">In de loop van de dertiende eeuw gingen ook de steden een rol spelen als vestigingsplaats van
        religieuze gemeenschappen. De internationale bedelorden zochten de bevolkingscentra op, waarbij het grootste
        aandeel werd genomen door de franciscanen en de dominicanen. De augustijner eremieten, de karmelieten, de
        eksterbroeders en de zakbroeders waren met kleinere aantallen conventen vertegenwoordigd, en laatstgenoemde twee
        orden verdwenen alweer toen ze door het concilie van Lyon verboden werden (1271).</p>

    <p class="indent">Ook het fenomeen van de begijnen gaat op de
        dertiende eeuw terug. Op hen is het predikaat 'eerste religieuze
        vrouwenbeweging' van toepassing. Hun achtergrond moet in de stedelijke burgerij
        worden gezocht, die ook het draagvlak vormde voor de bedelorden. In het westen
        en zuiden kwamen begijnhoven tot stand zoals die in het tegenwoordige Belgi&euml;
        nog bestaan; er is dan in het algemeen maar &eacute;&eacute;n begijnhof per stad. In het
        oosten woonden ze in begijnhuizen, waarvan er per stad meerdere tegelijk konden
        bestaan. Dit patroon overheerste in het Duitse Rijnland, maar uit het Repertorium van Simons (2001) wordt
        duidelijk dat het aanvankelijk ook in het zuidwesten van de historische Nederlanden voorkwam.</p>

    <p class="indent">Gedurende het eerste driekwart van de veertiende eeuw ging de groei van het aantal kloosters in
        een langzamer tempo. De begijnen kregen enkele huizen van begarden, hun mannelijke tegenhangers, naast zich,
        maar gingen door een diepe crisis als gevolg van de besluiten van het Concilie van Vienne (1311). Kortstondig
        waren er een paar vestigingen van lazarieten, die verwant waren met de geestelijke ridderorden. De toename van
        het aantal johannieter commanderijen zette door. Eveneens in de veertiende eeuw maakte Nederland voor het eerst
        kennis met de kartuizers en de kruisheren (bij de laatstgenoemde ging het om de ‘Belgische’ kruisheren, die Hoei
        als bakermat hadden). Opvallend was het grote aantal nieuw gestichte seculiere kapittels, waarvoor wereldlijke
        regionale en lokale heren verantwoordelijk waren. Als gevolg van veranderingen in de agrarische economie maakte
        bij de plattelandsorden de directe eigen exploitatie door middel van uithoven plaats voor indirecte exploitatie
        door verpachting.</p>

    <p class="indent">Vanaf de laatste decennia van de veertiende eeuw beleefde Nederland niets minder dan een
        monastieke revolutie, evenals trouwens de omliggende gebieden Brabant, Luik, Nederrijn en Westfalen. Deze
        omwenteling bevatte een kleine actieve component: vanuit het zuiden bereikten ons in deze periode de
        cellebroeders, die zich aan de verpleging wijdden. Overigens heeft in ons land de inzet van religieuzen voor de
        verpleging geen hoge vlucht genomen; het aantal gemeenschappen van hospitaalzusters was minimaal. In overwegende
        mate had de ontplooiing van het kloosterleven tegen 1400 dus een contemplatief karakter. Hiervoor was de Moderne
        Devotie verantwoordelijk, die men traditioneel laat beginnen met het publieke optreden van Geert Grote van
        Deventer (1379-1383). De Moderne Devotie was een stedelijk fenomeen; ook als nieuwe kloosters van regulieren
        zich op het platteland vestigden, hadden ze gewoonlijk nauwe banden met de burgerij van een nabije stad. Het
        IJsseldal geldt als de bakermat van de beweging, die hier vanwege een uitzonderlijk rijke historiografische
        overlevering goed is gedocumenteerd. Maar het westen van Nederland (Holland en het Nedersticht) nam vanaf het
        begin een gelijkwaardig aandeel; de urbanisatie had hier in de voorgaande decennia een hoge vlucht genomen.</p>

    <p class="indent">De Moderne Devotie in strikte zin kreeg haar neerslag in drie typen religieuze vestigingen. De
        broeders en zusters des gemenen levens woonden samen zonder een kerkelijk erkende regel; formeel waren zij dus
        leken. De tertianen en tertiarissen volgden de Derde Regel van Franciscus. Die was oorspronkelijk bedoeld
        geweest voor leken in de wereld, maar werd nu gehanteerd als leefregel voor religieuze gemeenschappen. Over hun
        status – leek of religieus – bestond destijds en bestaat ook nu nog veel verwarring. Een volledig kloosterlijk
        leven, gebaseerd op de klassieke drie geloften, werd geleid door de reguliere kanunniken en kanunnikessen. In de
        kloosterlijke tak was er een numeriek overwicht van mannen. Maar in de niet-kloosterlijke takken domineerden de
        vrouwen, met name bij de tertiarissen: er kan hier van een ‘tweede religieuze vrouwenbeweging’ worden
        gesproken.</p>

    <p class="indent">Het beginstadium van de Moderne Devotie werd gekenmerkt door experimenten. Het ‘gemene’
        (gemeenschappelijke) leven werd dikwijls stapsgewijs ingevoerd: vaak ging er een fase aan vooraf waarin broeders
        of zusters wel religieus samenwoonden maar met handhaving van privébezit. Hier en daar ontstonden
        priestergemeenschappen die zich al dan niet ontwikkelden tot huizen van broeders des gemenen levens. Al vanaf
        1399 gingen veel zusterhuizen over naar de tertiarissenstatus, soms wel soms niet met behoud van privébezit,
        maar met een belofte van kuisheid die in de oorspronkelijke Derde Regel niet was voorzien. De regulieren vormden
        vanaf 1395 het Kapittel van Windesheim. Een kwart eeuw later kwam daar het kleinere Hollandse Kapittel of
        Kapittel van Sion naast, dat zijn oorsprong had in enkele tertianenconventen die naar de regel van Augustinus
        overgingen. De tertianen en tertiarissen hadden al vanaf 1401 een eigen koepel, het Kapittel van Utrecht. Het
        werkgebied daarvan was beperkt tot het bisdom Utrecht; voor het bisdom Luik kwam daar in 1441 het Kapittel van
        Zepperen bij. Rectoren van broeder- en zusterhuizen kwamen regelmatig bij elkaar in het Colloquium van
        Zwolle.</p>

    <p class="indent">De Moderne Devotie was, zeker wat de kloosterlijke tak betreft, een observantiebeweging waarin
        terugkeer naar de getrouwe navolging van de regel (die van Augustinus) centraal stond. Er is aanleiding om aan
        het begrip ‘Moderne Devotie’ een nog wat grotere uitbreiding te geven dan de genoemde drie takken. De moderne
        devoten zelf oriënteerden zich sterk op de kartuizers, bij wie nooit regelverslapping was opgetreden en die in
        ons land parallel aan de Moderne Devotie een opvallende uitbreiding meemaakten. De kruisheren voerden in 1410
        een hervorming in observante geest door, geïnspireerd door de Moderne Devotie. De cisterciënzers kregen een
        observante tak in de gedaante van de Colligatie van Sibculo, die eveneens voortkwam uit de Moderne Devotie.
        Verder absorbeerde deze beweging ook een aantal bestaande begijnhuizen (maar lang niet alle). In de loop van de
        vijftiende eeuw werden oudere kloosters van reguliere kanunniken – met name in Friesland – hervormd vanuit
        Windesheim, waarbij ze hun status als dubbelklooster moesten opgeven.</p>

    <p class="indent">Afgezien van de vestiging van een zestal kloosters van de orde van Sint-Birgitta – in principe
        waren dit dubbelkloosters, conform de wens van de stichteres – en van enkele kloosters van sepulcrijnen en
        sepulcrinessen – hun bestaansrecht was gelegen in de bevordering van de verering van het Heilig Graf van
        Jeruzalem - werd de verdere vijftiende eeuw gekenmerkt door drie trends. Allereerst doorliepen binnen de sfeer
        van de Moderne Devotie veel kloosters, in wisselend tempo, een proces van claustralisatie: eenvoudige
        gemeenschappen met een lekenachtergrond namen eerst de derde regel van Franciscus aan, lieten zich vervolgens
        besluiten en gingen uiteindelijk in veel gevallen over naar een echte kloosterregel. De stimulans tot deze
        ontwikkeling kwam tegelijk van onderop, vanuit de zusters zelf, en van bovenaf. De reis van de pauselijke gezant
        Nicolaas van Cusa van 1451 leidde in veel gemeenschappen tot de min of meer gedwongen aanneming van de regel van
        Augustinus. Vanaf circa 1459 gingen ook de cellebroeders naar deze regel over; ze kregen gezelschap van een
        aantal conventen van cellezusters.
    </p>

    <p class="indent">Een tweede fenomeen was in deze periode de enorme toename van het aantal ‘low budget’-kloosters,
        waartoe men trouwens ook veel tertiarissenconventen al kan rekenen. De meeste waren te vinden in de steden, maar
        ze kwamen ook voor op het platteland, onder meer dat van Friesland. Deze kloosters beschikten over weinig
        inkomen uit onroerend goed en renten, zodat de zusters moesten werken voor hun brood, vaak in de textielbranche.
        Ze hadden daarom geen tijd voor het koorgebed, waartoe ook hun eenvoudige ongeschoolde afkomst hen ongeschikt
        maakte. Een groot aantal van deze kloosters leefde vanaf het midden van de eeuw eveneens volgens de regel van
        Augustinus, maar werd - bij ontbreken van koorzusters – dan alleen bewoond door conversinnen. Hun zielzorg
        ontvingen ze meestal van de – toch al overbelaste - mannenkloosters van de kapittels van Windesheim en Sion,
        maar aan volledige incorporatie viel niet te denken.Van enkele kloosters staat het vast dat ze bestemd waren
        voor conversinnen, bekeerde vrouwen, in engere zin: ex-prostitu&eacute;es.</p>

    <p class="indent">Ten slotte kwam kort voor het midden van de eeuw een nieuwe golf van observantisme op gang, die
        ditmaal de bedelorden betrof, met name de franciscanen en de dominicanen. Bij de franciscanen leidde deze tot de
        vestiging van een netwerk van nieuwe kloosters onder vicarissen, dat wil zeggen onder een parallelle organisatie
        binnen de ordeprovincie. Slechts weinige bestaande kloosters gingen tot deze observantie over, en die dan in
        sommige gevallen nog kwaadschiks, met verdrijving van de oude kloosterbevolking. Bij de dominicanen ging het
        geleidelijker; hier ontstond de observante Hollandse Congregatie. Beide observante orden gaven de stoot tot een
        ‘eigen’ netwerk van tertiarissenconventen; die van de franciscanen sloten zich niet aan bij het uit de Moderne
        Devotie voortgekomen Kapittel van Utrecht. Aan franciscaanse zijde kwamen er ook kloosters van ‘arme’ clarissen;
        de ‘rijke’ claren waren in ons land nauwelijks vertegenwoordigd. De observantie in de bedelorde genoot de
        bijzondere gunst van de Bourgondische en Habsburgse landsheren.</p>

    <p class="indent">Vanaf het tweede decennium van de zestiende eeuw was de bloei van het kloosterwezen voorbij.
        Nieuwe stichtingen kwamen nauwelijks nog voor, van de bestaande kloosters kwamen er vele in de problemen. De
        roepingen liepen terug, en hetzelfde geldt voor de begunstiging door particulieren en overheden. Stadsbesturen
        en de centrale regering in Brussel draaiden voor de religieuzen de fiscale duimschroef aan. Kloosters werden
        soms al opgeheven lang voor het begin van de Opstand, of ze werden tot fusie gedwongen. De oprichting van nieuwe
        bisdommen in 1559 werd mede bekostigd door ruim van goederen voorziene mannenklooster bij de kathedrale
        kapittels te incorporeren.</p>

    <p class="indent">Voor een groot deel van Nederland kwam het einde met de Opstand tegen Spanje en de installatie van
        de gereformeerde religie als publieke godsdienst. De chronologie van dit proces volgde de politiek-militaire
        loop der gebeurtenissen en verschilde dus per gewest of streek. De kloosters werden opgeheven, de mannelijke
        kloosterlingen voor de keuze tussen een eed van trouw of vertrek gesteld, voor de vrouwelijke werd een
        alimentatieregeling ontworpen. De goederen en inkomsten van de kloosters werden meestal toegewezen aan
        instellingen in de charitatieve sfeer, en voorzover de gebouwen nog stevig overeind stonden, kregen ook zij een
        nieuwe bestemming.</p>

    <p class="indent">In enkele gebieden werd het kloosterleven nog voortgezet tot diep in de zeventiende en zelfs tot
        in de achttiende eeuw. Twente ging enkele malen heen en weer tussen Spanje en de Republiek en werd pas met de
        Vrede van Münster (1648) definitief bij de provincie Overijssel gevoegd. De aanhechting van Staats-Vlaanderen,
        het huidige Zeeuws-Vlaanderen, werd voltooid met de herovering van Hulst door de Republiek in 1645. In deze
        regio’s wisten kloosters hun bestaan nog enige tijd te rekken.</p>

    <p class="indent">Nog ingewikkelder was de situatie in de huidige provincies Noord-Brabant en Limburg.
        ’s-Hertogenbosch werd in 1629 door de Republiek veroverd, waarop ook heel de Meierij onder het gezag van de
        Staten-Generaal kwam. Door de Maasveldtocht van 1632 kreeg de Republiek vaste voet in Maastricht. En in 1637
        werd Breda, dat in 1625 weer onder Spaans gezag was gebracht, terugveroverd door de Republiek. De aangehechte
        gebieden werden als Generaliteitslanden bestuurd vanuit de Verenigde Provincies. Maar dit betekent niet dat
        Noord-Brabant en Limburg in hun tegenwoordige gedaante in hun geheel een protestants regime kregen opgelegd. In
        Noord-Brabant lagen enkele buitenlandse enclaves: Megen, Gemert, Boxmeer en met name het Land van Ravenstein.
        Hier kon het rooms-katholieke kloosterleven nog worden voortgezet; uit de Meierij verdreven klooster vonden er
        hun toevlucht. De Baronie van Breda en het Land van Cuijk behoorden wel bij de Republiek, maar werden als min of
        meer zelfstandige heerlijkheden bestuurd door het huis van Oranje, dat er de kloosters de hand boven het hoofd
        hield.</p>

    <p class="indent">De provincie Limburg bestond als zodanig nog helemaal niet. De noordelijke helft werd gevormd door
        Opper-Gelre, dat onderdeel was van de Zuidelijke Nederlanden totdat het bij de Vrede van Utrecht (1713) in
        drieën werd gedeeld: een Oostenrijks, een Pruisisch en een Staats gedeelte. Elk van deze deelgebieden kende een
        eigen religieus regime. Andere onderdelen van ‘Limburg’ behoorden bij Gulik of bij het prinsbisdom Luik. De
        verovering van Maastricht maakte geen eind aan het tweeherig karakter van die stad: naast de Republiek als
        opvolger van de hertog van Brabant hield ook de bisschop van Luik er gezag, zodat het kloosterleven er intact
        bleef. De Landen van Overmaze (Valkenburg, ’s-Hertogenrade en Dalhem) werden bij het Partagetractaat van 1661
        elk verdeeld in een Staats en een Habsburgs gedeelte.</p>

    <p class="indent">Tegen deze achtergrond konden ruim zeventig uit de Middeleeuwen stammende kloosters ten zuiden van
        de grote rivieren hun bestaan in de Nieuwe Tijd voortzetten, niet zelden opgejaagd van de ene verblijfplaats
        naar de andere. Bovendien ontstond een veertigtal nieuwe kloosters, meestal behorend tot jonge, met de
        Contra-Reformatie verbonden orden, zoals die van de kapucijnen en de jezuïeten bij de mannen, de annuntiaten, de
        ongeschoeide karmelitessen (theresianen), de penitenten-recollectinen van de Reform van Limburg, en de ursulinen
        bij de vrouwen. Het einde kwam in de laatste decennia van de achttiende eeuw. In 1773 hief paus Clemens XIV
        onder druk van ‘verlichte’ Europese vorsten de orde van de jezuïeten op. De kloosterwetgeving van de
        Oostenrijkse keizer Jozef II (1783) schafte alle niet-actieve kloosters af; dat werd in ons gebied met name in
        Roermond gevoeld. Met de successievelijke inschakeling van de rest van het zuiden in het revolutionaire
        Frankrijk werd daar de op opheffing gerichte Franse wetgeving van kracht.</p>

