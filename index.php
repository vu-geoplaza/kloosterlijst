        <a name="inl"><h3 class="indent">Inleiding</h3></a>

    <p class="indent">Deze Kloosterlijst wil antwoord geven op de vraag welke kloosters er in ons land tijdens de
        Middeleeuwen en in de Nieuwe Tijd zijn geweest, en welke dus niet. Het begrip &lsquo;klooster&rsquo; wordt
        daarbij ruim genomen, zonder acht te slaan op kerkrechtelijke finesses die onderscheid maken tussen kloosters,
        conventen, huizen enzovoort. Alle gemeenschappen van mannen of vrouwen die voor een bijzonder religieus leven
        hebben gekozen, zijn in het hoofdbestand <strong>Kloosters </strong>opgenomen, inclusief de begijnhuizen en
        -hoven. Aan kloosters, conventen of religieuze huizen waarvan het bestaan op grond van de bronnen is komen vast
        te staan, is een unieke identificatiecode toegekend. Deze vormt de sleutel tot een record waarin – voorzover
        beschikbaar - enkele standaardgegevens zijn opgenomen (zie hieronder voor een toelichting op de structuur van
        het record). Over kloosters die in de moderne literatuur wel worden genoemd maar voor het bestaan waarvan geen
        bevestiging in de bronnen kon worden gevonden, is negatief beslist. Zij zijn opgenomen in een afzonderlijke
        <strong>Eliminatielijst</strong>, waarin de afwijzing telkens beknopt wordt beargumenteerd. Voor drie typen aan
        de kloosters verwante instellingen zijn afzonderlijke bestanden toegevoegd: voor <strong>Uithoven en
            refugia</strong>, voor <strong>Termijnhuizen </strong>en voor <strong>Kapittels </strong>(zie onder).</p>
    <p class="indent">De aanleiding tot het samenstellen van de Kloosterlijst lag in de door veel gebruikers gevoelde
        onvrede over het enige als uitputtend bedoelde naslagwerk inzake Nederlandse kloosters uit de Middeleeuwen en de
        Nieuwe Tijd, het <em>Monasticon Batavum </em>van M. Schoengen (1941/2). Schoengen is overleden voordat hij zijn
        naslagwerk kon voltooien. De redacteurs die zich hebben ingespannen voor het uitwerken van zijn aantekeningen,
        P.C. Boeren en D. de Kok, zijn daarin niet altijd geslaagd, met soms grote verwarring als gevolg. In het <em>Monasticon
            Batavum </em>is er bovendien voor gekozen elk stadium uit de geschiedenis van een klooster in een apart
        lemma onder te brengen, waardoor de gegevens over hetzelfde klooster soms erg verspreid terecht zijn gekomen.
        Recent onderzoek - zie vooral Van Engen (2006) - heeft de ogen geopend voor de verkloostering die zich met name
        in de vijftiende eeuw voordeed: veel religieuze huizen ondergingen stapsgewijs de ontwikkeling naar een steeds
        intenser kloosterleven en wisselden daarbij nogal eens van status of orde. In de Kloosterlijst krijgt elk
        klooster, zolang continuïteit van de kloosterbevolking mag worden aangenomen, niet meer dan één lemma (artikel,
        record). Daarbinnen worden dan diverse stadia onderscheiden. Alleen wanneer de oude bewoners integraal door
        nieuwe worden vervangen, wordt een nieuw lemma ingevoegd.</p>
    <p class="indent">De Kloosterlijst is geen <em>monasticon</em>. Anders dan in het <em>Monasticon Batavum </em>of
        bijvoorbeeld het meer recente <em>Monasticon Windeshemense </em>is geen uitputtende analyse van de geschiedenis
        van elk klooster nagestreefd en evenmin een volledige opgave van bronnen of literatuur. Slechts enkele
        basisgegevens worden verstrekt, zodat de gebruiker geörienteerd raakt en de weg gewezen krijgt naar voortgezet
        eigen onderzoek. Literatuur die al is vermeld in het <em>Monasticon Batavum </em>wordt, op enkele uitzonderingen
        na, niet opgenomen, en hetzelfde geldt voor gedetailleerde bronvermeldingen. Daar staat tegenover dat in de
        Kloosterlijst systematisch kan worden gezocht op orde, plaats (&lsquo;parochie&rsquo;), aliasnaam,
        patroonheilige, middeleeuws bisdom, historisch gewest (afzonderlijk voor Middeleeuwen en Nieuwe Tijd), en
        gender, en op combinaties daarvan; bovendien op begin- en einddatum.</p>
    <p class="indent">Dit betekent dat bestaande <em>monastica </em>hun functie blijven behouden; er wordt in het
        hoofdbestand <strong>Kloosters </strong>dan ook systematisch naar verwezen. Dit betreft het <em>Monasticon
            Windeshemense</em> voor de regulieren en regularissen van Windesheim, het <em>Monasticon Fratrum Vitae
            Communis </em>voor de broeders des gemenen levens, Van Dijk (2007b) voor de kloosters aangesloten bij de
        colligatie van Sibculo, Van Eeghen (1941) voor de Amsterdamse vrouwenkloosters, Gruijs (1975) voor de kartuizen,
        Van Moolenbroek (1985) voor de cisterciënzer vrouwenkloosters, Nyberg (1965) voor de birgittijnse kloosters,
        Kunzelmann (1972) voor de augustijner eremieten, Roggen (1995) voor die van de clarissen, Schlegel en Hogg
        (2004/5) wederom voor de kartuizen, Simons (2001) voor de begijnhuizen en -hoven in het zuiden van ons land,
        Tromp (1989) en Hillenga en Kroeze (2011) voor de kloosters in Groningen en de Ommelanden, Wolfs (1984) en
        (1988) respectievelijk voor de dominicanen en dominicanessen, en Jacobs (2011) voor de karmelieten en
        karmelitessen. Recent verscheenn Holt (2015), een publieksboek over de cisterciënzers met een stevige
        wetenschappelijke basis, en Hoebens (2017), een fraai uitgevoerd overzicht over de kloosters in Limburg: ook
        deze zijn verwerkt. Merkwaardig is dat voor de franciscanen geen samenhangend monasticon is verschenen. Het oude
        overzicht van Schoengen (1927) was al in het <em>Monasticon Batavum </em>verwerkt, en in de jaren &rsquo;50
        verschenen afzonderlijke artikelen over de meeste franciscaanse kloosters in de <em>Bijdragen voor de
            Geschiedenis van de Provincie der Minderbroeders in de Nederlanden</em>: deze literatuur wordt hier
        eveneensgenoemd. Ook zijn verwijzingen opgenomen naar het digitale <em>Monasticon Trajectense </em>voor de
        tertiarissen in het bisdom Utrecht, dat nog in opbouw is (<a href="http://www2.let.vu.nl/oz/monasticon/">http://www2.let.vu.nl/oz/monasticon/</a>
        ). Voor veel categorieën kloosters is het <em>Monasticon Batavum</em> op dit moment nog steeds het enige
        wetenschappelijke naslagwerk. De samensteller van deze Kloosterlijst spreekt de hoop uit dat zijn werk als
        controle en correctie op het <em>Monasticon Batavum </em>zal functioneren. Parallel aan de Kloosterlijst is voor
        de kloosters tot 1800 nu ook het digitale naslagwerk van het Erfgoedcentrum Nederlands Kloosterleven
        beschikbaar. Op de verhouding tussen de Kloosterlijst en de encyclopedie van het ENK wordt hieronder en in het
        document &lsquo;Kloosters voor en na 1800&rsquo; (zie Menu) nader ingegaan.</p>

    <a name="vv">
        <h3 class="indent">Voorgaande versies</h3></a>
    <p class="indent">In december 2008 werd de eerste versie van de Kloosterlijst op het web gezet. Daarna zijn,
        voorafgaande aan de huidige herziening, revisies verschenen in april 2010, september 2012, winter 2014/5,zomer
        2016 en maart 2017. Vanaf het begin is Ben Stuyvenberg als ict-er bij het project betrokken.</p>
    <p class="indent">In de loop van de tijd zijn onjuistheden en slordigheden gecorrigeerd, opengebleven velden verder
        ingevuld, en nieuw ontdekte relevante literatuur toegevoegd. Ook werd het aantal zoekmogelijkheden uitgebreid.De
        velden, oorspronkelijk aangeduid met tweeletterige sigla, kregen voluit geschreven benamingen. In september 2012
        onderging de <strong>Eliminatielijst</strong> een forse uitbreiding nadat was gebleken dat kloosters die ten
        onrechte voorkomen op de Kaart van de Kerkelijke Indeling door Muller, Joosting, Van Veen en Beekman – onderdeel
        van de <em>Geschiedkundige Atlas van Nederland </em>(1915-1923) – en die reeds door Schoengen al dan niet
        stilzwijgend naar het rijk der fabelen waren verwezen, toch weer de kop dreigden op te steken.</p>
    <p class="indent">Bij opeenvolgende herzieningen werden de records van het hoofdbestand met enkele velden
        uitgebreid. Toegevoegd zijn de numerieke velden &lsquo;Eerste Vermelding&rsquo; en &lsquo;Laatste Vermelding&rsquo;,
        die een chronologische zoekactie via het zoekmenu mogelijk maken. De jaartallen in deze beide velden zijn
        ongekwalificeerd: voor een toelichting moeten steeds de velden &lsquo;Stadia&rsquo; en &lsquo;Beëindiging
        kloosterleven&rsquo; worden geraadpleegd. In het veld &lsquo;MeMO&rsquo; wordt verwezen naar de digitale
        database Medieval Memoria Online, die in januari 2013 werd gelanceerd.Vermeld worden nu ook de huidige
        gemeenten, het voorkomen van een klooster op de desbetreffende kaart van Jacob van Deventer, en de opneming in
        het register van Rijksmonumenten. Aan het veld &lsquo;Stadia&rsquo; is een veld &lsquo;Filiatie&rsquo;
        toegevoegd waarin voor elk klooster gegevens zijn ondergebracht met betrekking tot de rol van eerder bestaande
        kloosters in zijn ontstaansfase.</p>
    <p class="indent">Nieuw zijn sinds de winter van 2014/5 de foto&rsquo;s die bij een groot aantal kloosters zijn
        opgenomen. Ze hebben de bedoeling de gebruiker te attenderen op overblijfselen van de kloosters die nog in de
        publieke ruimte zichtbaar zijn.</p>
    <p class="indent">Aan de kloosterbeschrijvingen werden vier velden toegevoegd om aan gegevens over archeologische
        status en activiteiten een plaats te geven. Voorlopig konden die alleen voor de provincie Noord-Holland worden
        ingevuld.</p>
    <p class="indent">Bij de laatste herzieningen is het principe dat beschrijvingen (pas) worden gesplitst bij
        discontinuïteit van de kloosterbevolking systematischer doorgevoerd. Dit had met name gevolgen voor kloosters
        van franciscanen waar de invoering van de observantie met verdrijving van de oude kloosterbevolking gepaard
        ging. Een voorbeeld is Kampen: er zijn nu twee lemmata voor de Kamper franciscanen (K01 en K14). </p>
    <p class="indent">In de zomer van 2016 is de Kloosterlijst uitgebreid met een apart bestand met (seculiere)
        kapittels. De belangrijkste verandering bij de laatste voorgaande revisie, die van maart 2017, werd gevormd door
        de lancering van de interactieve Kloosterkaart via UBVU/ Geoplaza (zie onder).</p>


    <a name="senk">
        <h3 class="indent">Samenwerking met het Erfgoedcentrum Nederlands Kloosterleven</h3>
    </a>
    <p class="indent">De Kloosterlijst is ontstaan als bijproduct van wetenschappelijk onderzoek naar aspecten van de
        Nederlandse kloostergeschiedenis. De gebruikers waarop de lijst zich richt zijn in eerste instantie de
        vakgenoten. Inmiddels is duidelijk geworden dat de Kloosterlijst ook een meer publieke functie kan vervullen.
        Sinds de zomer van 2015 wordt samengewerkt met het Erfgoedcentrum Nederlands Kloosterleven in Cuijk – Sint
        Agatha (<a href="http://www.erfgoedkloosterleven.nl/">http://www.erfgoedkloosterleven.nl/</a> ). </p>
    <p class="indent">Onder regie van Marga Arendsen werken medewerkers daar aan een digitaal naslagwerk voor de
        kloosters in Nederland vanaf 1800, bestemd voor een breed publiek. Het plan werd opgevat om dat naslagwerk uit
        te breiden met een onderdeel over de Middeleeuwse kloosters dat op de Kloosterlijst is gebaseerd. Toon Smeenk
        heeft de omvangrijke taak op zich genomen om de beschrijvingen van de VU-Kloosterlijst aan te passen voor
        hergebruik in het naslagwerk van &lsquo;Sint Agatha&rsquo;. Dit leidde in de zomer van 2016 en het voorjaar van
        2017 al tot de verwerking van een groot aantal correcties en aanvullingen in de Kloosterlijst zelf. De
        voorspoedige voortgang van deze werkzaamheden gaf aanleiding tot het voornemen om in beide naslagwerken dan ook
        beschrijvingen op te nemen van de kloosters uit de tussenliggende periode (ca. 1600-1800).</p>


    <a name="h17">
        <h3 class="indent">Herziening 2019</h3>
    </a></p>
    <p class="indent">De herziene versie die nu (zomer 2019) op het web wordt geplaatst, en die als definitief is
        bedoeld, bevat weer enkele belangrijke vernieuwingen. In de eerste plaats is nu ook de periode van de Republiek
        verwerkt. In de zeventiende en achttiende eeuw ontstond er een veertigtal nieuwe kloosters in ons land, die nu
        een eigen beschrijving hebben gekregen. Ruim zeventig uit de Middeleeuwen stammende kloosters zetten hun bestaan
        tijdens de Republiek voort: waar nodig zijn de beschrijvingen daarop aangepast. Het tijdsbereik van de
        Kloosterkaart is verlengd met de zeventiende en de achttiende eeuw.</p>
    <p class="indent">In de laatste voorgaande versies waren al vier velden met archeologische gegevens toegevoegd, maar
        nog grotendeels leeg gelaten. In deze nieuwe versie zijn ze systematisch ingevuld. </p>
    <p class="indent">Het bestand met Uithoven is sterk uitgebreid (dankzij inbreng van het ENK). De talrijke
        beslissingen die moesten worden genomen om locaties wel of niet in de Uithovenlijst op te nemen, leidden ook
        hier tot een reeks eliminaties. Die zijn, met kort commentaar, toegevoegd aan de Eliminatielijst. De Uithoven
        zijn nu ook aan de Kloosterkaart gekoppeld, en hetzelfde geldt voor de Kapittels.</p>
    <p class="indent">Gelijktijdig met de lancering van deze nieuwe versie van de VU-Kloosterlijst wordt het naslagwerk
        &lsquo;Kloosters in Nederland&rsquo; van het ENK uitgebreid met records voor alle kloosters tot 1800. Die hebben
        een andere opzet dan de records van deze Kloosterlijst, met als belangrijk verschil dat in de ENK-versie korte
        verhalende teksten zijn toegevoegd. Inhoudelijk zijn de beide versies zo goed mogelijk gecoördineerd. De records
        in het naslagwerk van het ENK zijn standaard voorzien van verwijzingen (geen hyperlinks) naar die van de
        VU-Kloosterlijst , en omgekeerd is aan de Kloosterlijst een veld met referenties naar het ENK toegevoegd.</p>

    <a name="arch"><h3 class="indent">Archeologie</h3></a>
    <p class="indent">Opgravingen en archeologische waarnemingen hebben in onze middeleeuwse kloosters ook vroeger al
        wel plaatsgevonden, maar dankzij het Verdrag van Malta (1992) is dit onderzoeksterrein in een stroomversnelling
        geraakt. Oudere monastica hadden nog weinig aanleiding om aandacht te besteden aan materiële bronnen voor het
        middeleeuwse kloosterleven. In recentere naslagwerken komt de registratie van archeologische gegevens wel op
        gang. Toch wordt de integratie tussen schriftelijke en materiële bronnen nog bemoeilijkt door de overgeleverde
        disciplinaire tweedeling tussen geschiedschrijving en archeologie. Daarom doet de Kloosterlijst een poging om
        deze splitsing te doorbreken. Met de verwerking van de stand van zaken op archeologisch gebied was in de laatste
        voorgaande versies al een begin gemaakt, maar het grootste deel van het werk hieraan is verricht in de periode
        2017-2019.</p>

    <p class="indent">Voor de archeologische gegevens zijn vier velden gereserveerd. Het eerste veld bevat verwijzingen
        (geen hyperlinks) naar de nummers op de Archeologische Monumentenlijst. Deze nummers zijn ook te vinden op de
        Archeologische Monumentenkaart die door de Rijksdienst voor het Cultureel Erfgoed op het Internet is geplaatst:
        <a href="https://archeologieinnederland.nl/amk-en-ikaw" target="_blank">https://archeologieinnederland.nl/amk-en-ikaw</a>.
        Het tweede veld registreert de status die officieel aan archeologische sites is toegekend: van archeologische
        waarde, van (zeer) hoge archeologische waarde, eventueel beschermd. In het derde veld is een korte omschrijving
        te vinden van de aard van de archeologische activiteiten en het jaar waarin ze hebben plaats gevonden. Voor de
        jongere activiteiten is daarbij zoveel mogelijk gebruik gemaakt van de professionele aanduidingen zoals die
        tussen 2001 en 2003 zijn ontwikkeld ten behoeve van de Kwaliteitsnorm voor de Nederlandse Archeologie (KNA) en
        in 2007 vastgelegd in de Wet op de Archeologische Monumentenzorg. Het vierde veld vermeldt de archeologische
        literatuur, waarvoor een aparte bibliografie aan de Kloosterlijst wordt toegevoegd.</p>

    <p class="indent">Dank komt toe aan Ivonne Lempke te Culemborg, die me welwillend haar databestand ter hand stelde,
        zodat met de verwerking van de archeologische gegevens een begin kon worden gemaakt. Toen deze verwerking in een
        gevorderd stadium was, stelde de RCE te Amersfoort een omvangrijk excerpt uit de gegevensbestanden van Archis
        ter beschikking. Dit betekende zowel een welkome onafhankelijke controle op de al verzamelde gegevens als een
        aanvulling daarop.</p>

    <a name="urtk"><h3 class="indent">Uithoven en refugia, termijnhuizen,
            kapittels</h3></a>

    <p class="indent">Sinds de herziening van september 2012 is de Kloosterlijst uitgebreid met enkele nieuwe
        deelbestanden, een met uithoven en refugia en een met termijnhuizen. Een deel van de verwarring die in de oudere
        literatuur heerst rond de identificatie van kloosters, is ontstaan doordat vermeldingen van dergelijke
        onzelfstandige kloosternederzettingen zijn opgevat als aanduidingen voor het bestaan van zelfstandige kloosters:
        in de eliminatielijst was daarover al het een en ander te vinden. Het lag nu voor de hand aan de Kloosterlijst
        overzichten van deze afhankelijke monastieke nederzettingen toe te voegen.</p>


    <p class="indent"><strong>Uithoven</strong> (in Groningen‘voorwerken’ genoemd en in het Latijn <em>grangiae</em>)
        ontstonden met de opkomst van de cisterciënzers en norbertijnen vanaf omstreeks 1100. De kloosters van deze
        nieuwe orden verwierven veel afgelegen agrarische bezittingen. Waar die geclusterd lagen, was het mogelijk
        uithoven te stichten: steunpunten voor directe agrarische exploitatie bemand door conversen (lekebroeders). Zie
        bijvoorbeeld Rösener (1982); Dekker (1982) 191vv; Lohrmann (1983); Mol (1991a) 175vv en Mol (2014 [2015]). Niet
        zelden groeiden dergelijke uithoven na verloop van tijd uit tot zelfstandige kloosters. Zo is het Duitse huis
        van Schoten (S16) begonnen als uithof van Nes (N05), zie Mol (1991a) 176; Selwerd (G34) werd gesticht op een
        uithof van Dikninge (I02): Tromp (1989) 23. Ook het omgekeerde kwam voor. Bakkeveen (B01) begon als uithof van
        Mariëngaarde (H27), werd toen een zelfstandige priorij van de norbertijnen, maar verloor die zelfstandigheid na
        1343 en werd opnieuw uithof van Mariëngaarde.</p>

    <p class="indent">Voor de identificatie en opneming van de uithoven moesten, evenals dat bij de kloosters zelf het
        geval was, talrijke beslissingen worden genomen. Voor verschillende grote plattelandskloosters kon op
        monografieën worden teruggevallen; een voorbeeld is Van Bavel (1993) voor Mariënweerd (B02). De grootste
        concentraties uithoven komen voor in de provincies Groningen, Friesland en Zeeland (met Zeeuws-Vlaanderen). Voor
        Zeeland beschikken we over de diepgaande studies van Gottschalk (1983) en (1984) en van Dekker (1982). Aan de
        uithoven in Friesland is een aparte kaartlaag in HISGIS Fryslân gewijd. Lastiger bleek de situatie voor
        Groningen (de Ommelanden). In Siemens (1962) is een uitvoerige en van fraai gekleurde kaarten voorziene
        reconstructie van de middeleeuwse kloostergoederen opgenomen. Deze gegevens zijn verwerkt door Tromp (1989) en
        vormen nu ook een aparte kaartlaag ‘kloostergoederen Groningen’ in HISGIS Fryslân
        (http://www.hisgis.nl/hisgis/gewesten/fryslan/fryslan) . Schroor heeft in (1996) en (1997) de achttiende-eeuwse
        kaarten van de landerijen van Land en Stad uitgegeven, waarvan de voormalige kloostergoederen een belangrijk
        deel uitmaken. Maar noch de reconstructie door Siemens, noch de door Schroor uitgegeven kaarten geven een
        vlakdekkende opsomming van de voorwerken. De beheerseenheden op de achttiende-eeuwse kaarten zijn eerder de
        ‘plaatsen’ of ‘heerden’, de zetels van de beklemde meiers. Maar deze eenheden corresponderen niet met de
        middeleeuwse voorwerken. Tromp daarentegen vermeldt wel een groot aantal voorwerken, maar zonder nauwkeurige
        verantwoording. Voor de methode om deze zo goed mogelijk te achterhalen kan men terecht bij de behandeling door
        Mol van de kloostergoederen van Aduard (F04) in Van Moolenbroek, Mol en Loer (2010). Voor Groningen leek het al
        met al raadzaam aan de voorzichtige kant te blijven en tamelijk veel vermeldingen van voorwerken naar de
        eliminatielijst te verwijzen.</p>

    <p class="indent">Grote plattelandskloosters bezaten tevens vaak <strong>stadshoven</strong> die als stapelplaatsen
        en verkooppunten dienden: Mol (1991a) 175; zie ook Van Bavel (1993) 28. Toen tegen het eind van de Middeleeuwen
        de directe agrarische exploitatie binnen het uithovenstelsel minder belangrijk werd, verschoof de functie van
        dergelijke stadse vestigingsplaatsen naar die van <strong><em>refugium</strong></em>: een pied-à-terre binnen de
        veilige stadsmuren. Met name in de tijd van de Opstand namen talrijke kloostergemeenschappen hun toevlucht tot
        een dergelijk <em>refugium</em>.</p>

    <p class="indent">In het deelbestand met uithoven en refugia zijn ook basisgegevens opgenomen van
        (strong>hoven</strong> die zich in het bezit hebben bevonden van oude benedictijner abdijen (zelf gewoonlijk
        gelegen buiten onze huidige landsgrenzen). Het gaat hier om hoven in de zin van het domaniale stelsel: eenheden
        van indirecte agrarische exploitatie waaraan ook rechtsmacht verbonden kon zijn. De benedictijnen bewerkten deze
        hoven dus niet zelf, maar gaven ze in handen van een wereldlijke <em>villicus</em> of meier, die zich in de loop
        van de tijd ten opzichte van het klooster kon verzelfstandigen. Zie bijvoorbeeld Dekker (1982) 66-80 en (1983)
        27-29; 98-126. Na het jaar 1100 raakte dit type in onbruik toen de verre abdijen hun grondbezit in ons land
        afstootten.</p>

    <p class="indent">Met het deelbestand van de <strong>termijnhuizen</strong> bevinden we ons in de sfeer van de
        bedelorden: franciscanen, dominicanen, karmelieten en augustijner eremieten. De kloosters van deze orden lagen
        vrijwel altijd in grotere, centraal gelegen steden. Van daar uit trokken de monniken er in de omgeving op uit om
        te prediken, biecht te horen en te bedelen, waarbij ze zich hielden aan de grenzen van het district dat hun door
        de ordesprovincie was toegewezen. ‘In zo’n <em>terminus</em> of termijngebied werden na verloop van tijd, in
        Vlaanderen omstreeks 1300, <em>domus terminarie</em> of termijnhuizen ingericht, soms niet meer dan een paar
        kamers in een bestaande woning, waar de broeders terminarii konden overnachten. In later eeuwen verbleef zo’n
        termijnbroeder quasi permanent in zijn termijnhuis’: Simons (1987) 187.</p>


    <p class="indent">Sinds zomer 2016 bevat de database een afzonderlijk bestand met (seculiere)
        <strong>kapittels</strong>. Kapittels zijn kerkelijke instellingen bevolkt door mannen – meestal, maar soms ook
        door vrouwen – die een (min of meer) gemeenschappelijk leven leiden waarin het bidden van de zeven getijden
        centraal staat, maar die niet de drie kloostergeloften van armoede, gehoorzaamheid en kuisheid afleggen. In
        plaats daarvan was hun leefwijze gebaseerd op de leefregel van Aken van 816. Kapittels waren vaak aan
        aanzienlijke kerken verbonden. De term ‘kapittel’ voor dit type instelling raakte overigens pas in de twaalfde
        eeuw in gebruik. Een synoniem is ‘stift’; de bewoners van zo’n instelling worden aangeduid als ‘kanunniken’ en
        ‘kanunnikessen’. Het gaat bij deze ‘kapittels’ dus om lokale instellingen, en niet om kapittels in de zin van
        bovenlokale verbanden van kloosters die tot eenzelfde orde behoren (zoals in ‘Kapittel van Windesheim’).</p>

    <p class="indent">In de Nederlandse wetenschappelijke literatuur is het gebruikelijk kapittels en kloosters strikt
        gescheiden te houden, en in eerdere veries van de Kloosterlijst is die gewoonte ook gevolgd. Bij de herziening
        van 2016 is besloten de kapittels toch op te nemen, om twee redenen. In de ons omringende landen, en met name in
        Duitsland, worden kapittels gewoonlijk wel vermeld in Klosterbücher en vergelijkbare overzichtswerken. Bovendien
        vonden ook in ons land overgangen van een kerkelijke instelling van het klooster- naar het kapitteltype plaats,
        en omgekeerd. Zo waren aan de kerk van Meerssen eerst kanunniken verbonden, die echter in de loop van de
        twaalfde eeuw geleidelijk werden vervangen door benedictijnen (M25). Daarentegen gingen de adellijke
        benedictinessen van Thorn (T03) in de loop van de tijd steeds meer de leefwijze van stiftsdames volgen. Aan het
        eind van de rooms-katholieke periode werden de Sint-Servaasabij (U07) en nog vier andere Utrechtse
        vrouwenkloosters omgezet in vrouwenkapittels, voortaan bewoond door protestantse juffers; zie Van Kalveen
        (1997). Voor de kapittels in het middeleeuwse bisdom Utrecht is sinds 2014 het uitvoerige repertorium van Jan
        Kuys beschikbaar. Voor die in het zuiden van het land heb ik gebruik gemaakt van enkele publicaties van
        Arnoud-Jan Bijsterveld, die ook zo vriendelijk was aanvullend ongepubliceerd materiaal toe te zenden. Voor de
        vroege kapittelstichtingen is Van Vliet (2002) onmisbaar. De beschrijvingen zijn beknopt gehouden. Kapittels
        speelden een relatief grote rol in de middeleeuwse <em>memoria</em>: niet minder dan vijftig van de ongeveer
        zeventig kapittels komen voor in MeMO: daarvoor is een apart veld opgenomen</p>

