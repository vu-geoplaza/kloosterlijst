    <p class="indent">
        The Topographical Index contains references to the various components of the Census. References to the main
        database of Monasteries are with ‘kloo’ (abbreviation for ‘klooster’ = monastery). The indications ‘uith-u’,
        ‘uith-r’ and ‘uith-h’ refer to the locations of granges, urban refuges and manors respectively included in the
        database Granges and Urban Refuges (‘uithof’ = grange). The monasteries in that same database on which the
        granges etc are dependent, are referred to with ‘uith-k’. Likewise, the locations of houses of terminarii and
        the monasteries on which they are dependent, are indicated by ‘term-t’ and ‘term-k’. The database with
        Collegiate Churches is referred to by ‘kap’ (‘kapittel’). With ‘elim’ the List of Eliminations is meant. If data
        on a specific toponym are found under a different toponym, this is mentioned introduced by an arrow.
    </p>
    <p class="indent">In the databases with Monasteries, Granges etc, Houses of Terminarii and Collegiate Churches, in
        principle the official modern spelling is applied. Historical toponyms are inserted in those cases in which
        their recognition presents difficulties. Official Frisian equivalents are not inserted because these mostly have
        a recent origin. Toponyms with prefixes such as Sint- are listed under the main component: Sint-Oedenrode e.g.
        is found under the –O-.</p>
    <p class="indent"><em>An example</em>: The item ‘Brielle’ yields: ‘kloo; uith-u; term-t; -> Rugge (kloo);
        Munsterkerk (kap)’. This means that data on Brielle are found under this toponym in the main database
        Monasteries (Field: Parish), among the granges and houses of terminarii in the relevant databases, in the main
        database additionally under the toponym Rugge and among the Collegiate Churches under Munsterkerk.</p>
