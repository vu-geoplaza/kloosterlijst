    <p class="indent"><strong>Edam, begijnhof: Muller, <i>Gesch. Atlas</i>
    I, 332; Nübel (1970) 112.</strong> Het zou gaan om de huidige
    Kleine Kerk. De bron is Van Heussen, <i>Hist ep. Harlem. </i>130, die aanknoopt
    bij De la Torre; zie ook Nübel (1970) 112. De la Torre vermeldt de OLV kerk;
    Van Heussen denkt dat dit de kerk van het begijnhof is waarvan hij het bestaan
    in Edam veronderstelt. Maar de OLV of Kleine Kerk, waarvan nu nog alleen de
    Speeltoren over is, is de voortzetting van een kapel die het oudste bedehuis
    van Edam was, gelegen op een terp ten westen van de Ye in de parochie Middelie.
    Met de stadwording verkreeg Edam in de loop van de veertiende eeuw een eigen aan
    Sint-Nicolaas gewijde parochiekerk: Boschma-Aarnoudse (2003) 82. Voorts speelt
    hier de bekende verwarring tussen tertiarissen en begijnen een rol. Het gaat
    dus om het tertiarissenconvent (E04) dat Maria Magdalena als patroonheilige
    had. Dit was gelegen direct ten zuiden van de Sint-Nicolaaskerk, zoals blijkt
    uit de bijkaart van Van Deventer, die daar de <i>sorores </i>vermeldt. De
    plaatsing van dit convent op het tegenwordige Bagijnenland (zo  Schoengen) is
    onjuist: de netkaart van Van Deventer laat zien dat het Bagijnenland, aan de
    overzijde van de gracht, rond 1560 nog onbebouwd is.</p>



    <p class="indent"><strong>Eemstein, franciscanen: Muller, <i>Gesch.
