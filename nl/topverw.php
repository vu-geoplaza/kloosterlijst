    <h3>Topografische index</h3>
    <hr class="indent">
    <p class="indent">
        De Topografische Index bevat verwijzingen naar de verschillende componenten van de Kloosterlijst. Naar het
        hoofdbestand met de ruim 700 klooster wordt verwezen met ‘kloo’. De aanduidingen ‘uith-u’, ‘uith-r’ en ‘uith-h’
        verwijzen respectievelijk naar de vestigingsplaatsen van uithoven, refugia en hoven opgenomen in het deelbestand
        Uithoven en Refugia; de kloosters in dat bestand waarvan deze uithoven, refugia en hoven afhangen worden
        aangeduid met ‘uith-k’. Op dezelfde manier worden de vestigingsplaatsen van termijnhuizen en de kloosters
        waarbij deze behoren aangeduid met de termen ‘term-t’ en ‘term-k’. Naar het bestand met kapittels wordt verwezen
        met ‘kap’. Met ‘elim’ is de Eliminatielijst bedoeld. Indien gegevens voor een gezocht toponiem te vinden zijn
        onder een ander toponiem, wordt dat genoemd voorafgegaan door een pijl.
    </p>
    <p class="indent">In de bestanden Kloosters, Uithoven en Refugia, Termijnhuizen en Kapittels wordt in principe de
        huidige officiële spelling gebruikt. Historische naamsvormen worden, met verwijzing, opgenomen voorzover ze
        moeilijk herkenbaar zijn. Er is van afgezien Friestalige equivalenten op te nemen, omdat die meestal van recente
        oorsprong zijn. Plaatsnamen met voorvoegsels als Sint zijn geplaatst op het hoofdwoord, bijvoorbeeld Sint
        Oedenrode onder de O.</p>
    <p class="indent"><em>Voorbeeld</em>: Gegevens over Brielle zijn onder dit toponiem te vinden in het hoofdbestand
        (veld: parochie), onder de uithoven en de termijnhuizen in de desbetreffende bestanden, in het hoofdbestand ook
        nog onder het toponiem Rugge, en in het bestand met kapittels onder Munsterkerk.</p>
