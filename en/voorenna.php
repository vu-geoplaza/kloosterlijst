        gegevensbestanden op terreinen als handschriftelijke overlevering, archieven,
        monumentenstatus en archeologisch onderzoek. Het ENK-naslagwerk richt zich op
        een algemeen geïnteresseerd publiek, dat de informatie behalve in enkele vaste
