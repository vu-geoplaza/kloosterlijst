    <h2 class="indent">VOOR EN NA 1800</h2>


    <p class='indent'>In de kloostergeschiedenis van
        Nederland kunnen we globaal onderscheid maken tussen de periode van de oudere
        en die van de jongere kloosters. De jongere kloosters bestaan nog of hebben tot
        voor kort bestaan. Ze zijn bijzonder talrijk en kunnen voor het grootste deel
        ‘actief’ worden genoemd, dat wil zeggen dat hun belangrijkste taak in missie,
        verpleging, onderwijs of andere vormen van apostolaat gelegen is. Een veel
        kleiner aantal is </p>

    <p class='indent'>beschouwelijk en ziet het gebed
        als hoofdtaak. De oudere kloosters hebben bestaan in de jaren voor 1800. De
        meeste daarvan behoren tot de Middeleeuwen, want na de invoering van de
        Reformatie hadden in het grootste deel van ons land kloosters geen recht van
        bestaan meer. In de oudere periode was het onderscheid tussen contemplatieve en
        actieve kloosters minder uitgesproken dan in
        de tijd na 1800: veel kloosters hadden een regel waarin contemplatie weliswaar
        het belangrijkste element was, maar waarin toch ook ruimte werd gelaten om een
        rol te spelen in de omliggende maatschappij. Een klein aantal uit de
        Middeleeuwen stammende kloosters bestaat nog steeds. Een daarvan is dat van de
        kruisheren in Cuijk-Sint Agatha, dat ook het Erfgoedcentrum Nederlands
        Kloosterleven (ENK) herbergt.  </p>

    <p class='indent'>De oudere kloosters worden beschreven in
        de Kloosterlijst van de Vrije Universiteit (VU). Het naslagwerk ‘Kloosters in Nederland’ van ENK
        beschrijft zowel de jongere als oudere kloosters, waarbij het laatste deelbestand is
        afgestemd met de VU Kloosterlijst.   Het document dat U nu leest, is bedoeld
        voor diegenen die vertrouwd zijn met de situatie na 1800 maar ook wel eens
        kennis willen nemen van het oudere kloosterleven, en voor degenen die juist de
        omgekeerde weg bewandelen. Met name tussen de zestiende en de negentiende eeuw
        traden ingrijpende veranderingen in het kloosterlandschap op, die het soms
        lastig maken om de oudere situatie te begrijpen vanuit bekendheid met de meer
        recente, en vice versa.  </p>

    <p class='indent'>Extra ingewikkeld
        wordt de geschiedenis van het kloosterleven door wisseling in termgebruik. Een
        overkoepelend kloosterverband werd in de Middeleeuwen vaak ‘kapittel’ genoemd,
        zoals het Kapittel van Windesheim en het Kapittel van Utrecht. Maar de term
        ‘kapittel’ kan ook van toepassing zijn op een college van wereldgeestelijken
        verbonden aan een grote kerk, zoals het Domkapittel in Utrecht. In de recentere
        periode wordt zo’n bovenlokale groep aangeduid als ‘congregatie’. Dat woord
        heeft dan meestal een specifieke betekenis, namelijk van een jonger
        kloosterverband dat onder het gezag van de bisschop valt en waarvan de leden
        slechts eenvoudige geloften afleggen (zie ook p. 6). Maar ook de oudste en
        meest eerbiedwaardige orde, die van de benedictijnen, kent congregaties. </p>

    <p class='indent'>Benedictijnerkloosters staan in
        principe los naast elkaar, maar groepjes kloosters konden binnen die orde
        federaties samenwerkingsverbanden vormen op
        grond vangeestverwantschap. Een voorbeeld is de Congregatie van
        Bursfeld die midden vijftiende eeuw bepaalde hervormingen doorvoerde. Eenzelfde
        type kloosterlingen wordt soms met verschillende benamingen aangeduid. Zowel in
        de Late Middeleeuwen als in de tijd na 1800 is een grote rol gespeeld door
        vrouwelijke volgelingen van de Derde Orde van Franciscus, maar in de
        Middeleeuwen worden zij ‘tertiarissen’ genoemd, in de jongere tijd ‘franciscanessen’. </p>

    <p class='indent'>Het stuk dat volgt
        schetst enkele belangrijke ontwikkelingen op de lange termijn en signaleert
        punten waarop de twee globale perioden van elkaar afwijken. Het document gaat
        ten slotte ook in op de verschillen in aanpak tussen de Kloosterlijst van de VU
        en het naslagwerk van het ENK. </p>

    

    <h3>Eén groot verhaal, werelden van verschil</h3>

    <p class='indent'>De historie van het kloosterleven
        heeft, wereldwijd maar zeker ook in Nederland, de eeuwen door in het teken
        gestaan van de spanning tussen twee tegengestelde tendensen. Steeds opnieuw
        ontstonden golven van enthousiasme om God en de naaste te dienen en aan dat
        dienstbetoon een kloosterlijke vorm te geven. De manier waarop de roepingen
        werden beleefd was aan voortdurende verandering onderhevig. Die variatie
        weerspiegelde de ontwikkelingen in de maatschappij. Het kloosterleven onderging
        zo steeds weer nieuwe impulsen tot vernieuwing. Tegenover deze van onderop
        komende innovaties was de houding van de kerkelijke leiding tweeslachtig. Aan
        de ene kant juichte de Kerk de roepingen toe en erkende ze die als
        ondersteuning van haar inspanningen tot kerstening van de samenleving. Maar
        tegelijk was de hiërarchie benauwd voor wildgroei, in het bijzonder wanneer ze
        vreesde dat nieuwe initiatieven met de rechtzinnigheid in gedrang zouden komen.
        De uitkomst van het op elkaar inwerken van deze twee krachten was een
        geschiedenis van het kloosterleven die zowel continuïteit als diversiteit
        vertoont. </p>

    

    <p class='indent'>Een geschikt startpunt voor dit
        overzicht is het Vierde Concilie van Lateranen, in 1215 gehouden onder leiding
        van de beroemde paus Innocentius III. Het concilie reageerde op een explosie
        aan religieuze bewegingen van onderop, maar beoordeelde een deel daarvan als
        onorthodox. Om grip te krijgen op de situatie besloot het om alle kloosterlijke
        gemeenschappen te verplichten zich te onderwerpen aan een van de bestaande
        kloosterregels. </p>

    <p class='indent'>In de praktijk kwam dat erop neer
        dat in West-Europa de regels van Benedictus (480-547), Augustinus (354-430) en
        Franciscus (ca. 1182-1226) een overheersende positie gingen innemen. Een aantal
        bestaande orden werd in de eeuw die op het concilie volgde zelfs afgeschaft.  </p>

    <p class='indent'>Gemeenschappelijk aan
        de kloosterregels was dat degenen die intraden en professie deden, de drie
        geloften van armoede, gehoorzaamheid en kuisheid aflegden. Om daarnaast toch
        ook variatie in kloosterspiritualiteit mogelijk te maken en deze in goede banen
        te leiden, konden de regels worden aangevuld met constituties waarin de details
        van de leefwijze in een bepaalde orde vorm kregen. Zo was de missionaire activiteit
        van de dominicanen, die stad en land afreisden om te prediken (‘predikheren’),
        gebaseerd op de regel van Augustinus, maar werd deze aangevuld door een
        Constitutie die was opgesteld door de ordestichter Dominicus Guzmán (1170-1221)
        en goedgekeurd door de paus. Met de franciscanen (minderbroeders) deelden de
        dominicanen niet de regel maar wel de toewijding aan het apostolaat, evenals de
        volstrekte armoede: het waren bedelmonniken. Maar dezelfde regel van Augustinus
        was eerder al aangenomen door sommige plattelandskloosters – in ons land
        bijvoorbeeld het bekende Rolduc – die over een groot goederenbezit beschikten
        en waarvan de bewoners hun hele leven aan hetzelfde klooster gebonden bleven. </p>

    <p class='indent'>De kerkleiding was en
        is tot op de huidige dag een aangelegenheid van mannen. Het verbaast daarom
        niet dat de Kerk altijd beducht is geweest om vrouwen die een religieus leven
        wilden leiden te veel vrijheid toe te kennen. Een ijkpunt is in deze samenhang
        de bul <i>Periculoso </i>van paus Bonifatius VIII (1298). Hierin wordt aan alle
        vrouwelijke religieuzen de clausuur (het kloosterslot) voorgeschreven. Vrouwen
        die intreden worden geacht de ommuring van het klooster niet meer te verlaten,
        buitenstaanders mogen zonder speciaal verlof het besloten klooster niet in. De
        clausuur hangt samen met de omstandigheid aanname dat
        vrouwelijke religieuzen, anders dan mannelijke, geen taak in de wereld zouden
        hebben. Hun leven is er een van godsbeschouwing,
        zelfheiliging en gebed: hun spiritualiteit is contemplatief. De voorbede voor
        verwanten en alle anderen die in de wereld blijven, tijdens hun leven en na hun
        dood, is de dienst die zij aan de samenleving bewijzen. En bedelen werd voor
        vrouwen natuurlijk veel te gevaarlijk geacht. De kloosters van de vrouwelijke
        ‘tweede’ orden die door Franciscus en Dominicus werden gesticht (de clarissen
        en de dominicanessen) kenden de clausuur en moesten dan uiteraard ook over
        bezittingen en inkomsten uit vermogen beschikken.
    </p>

    <p class='indent'>In de leefwijze van
        de mannen lag de relatie tussen actieve en contemplatieve elementen iets
        ingewikkelder. Kloosters met de regel van Benedictus – behalve de benedictijnen
        met name ook de cisterciënzers - waren in sterke mate contemplatief: ze waren
        de kampioenen van het getijdengebed. Maar ook zij dienden de samenleving
        vanouds op een meer directe manier, door bijvoorbeeld aan reizigers en pelgrims
        onderdak te bieden in hun gasthuizen. Hospitaalzorg was – althans in de
        beginfase – een belangrijke reden van bestaan voor de in 1113 in het Heilige
        Land gestichte orde van de johannieters. In mannenkloosters met de regel van
        Augustinus was ruimte gereserveerd voor zielzorg onder de leken. Om de
        sacramenten te kunnen bedienen waren veel leden van dergelijke kloosters niet
        alleen geprofest maar ook tot priester
        gewijd. Tot hen behoorden onder anderen de </p>

    <p class='indent'>premonstratenzers oftewel de
        norbertijnen<span style='color:#CE181E'>,</span> een orde gesticht door de
        ‘Nederlander’ Norbert van Gennep (ca. 1075-1134) waarvan de leden talloze
        plattelandsparochies bedienden, met name in
        Friesland en in Noord-Brabant. Omgekeerd waren de bedelorden weliswaar bij
        uitstek ‘actief’ – ze telden een groot aantal ‘paters’, monniken met de
        priesterwijding, die ijverig predikten en de biecht afnamen  - maar de
        krachtbron voor hun apostolaat vonden ze toch in het gebed, waarvoor ook hun
        dagritme voldoende ruimte bood. Tot de bedelorden rekenen we behalve de
        franciscanen en de dominicanen ook de karmelieten en de augustijner eremieten. </p>

    <p class='indent'>De verdere
        ontwikkeling van het leven van de religieuzen in de late Middeleeuwen zou niet
        verlopen volgens de scherpe contouren die in 1215 en 1298 waren getekend. Een
        belangrijke oorzaak was de rol die gespeeld zou worden door een omvangrijke
        groep van personen – mannen, maar vooral vrouwen – die weliswaar leken waren in
        de zin dat zij niet de drie klassieke kloostergeloften aflegden, maar die hun
        leven wel in het teken stelden van een verdiepte beleving van het geloof.
        Penitentie, boetvaardigheid, was de kern van hun bestaan. Sommige van deze
        ‘religieuze leken’ – zoals ze gemakshalve kunnen worden aangeduid -  woonden op
        zichzelf, eventueel zelfs in een gezin, andere gingen groepsgewijs samenwonen.
        Bekende vertegenwoordigers van deze ‘leken’ in de Nederlanden waren de begijnen
        en hun mannelijke tegenhangers, de begarden.  </p>

    <p class='indent'>De begijnen in een
        huis of hof hielden zich aan statuten die lokaal voor een dergelijke instelling
        waren vastgesteld, maar zij volgden geen door de Kerk universeel goedgekeurde
        regel. Dergelijke voor religieuze leken bestemde regels kwamen er toch ook. De
        belangrijkste was de door paus Nicolaas IV in 1289 goedgekeurde ‘derde regel’
        of penitentieregel, die met terugwerkende kracht aan Franciscus werd
        toegeschreven. De volgelingen van deze regel behoorden tot de ‘Derde Orde’ (naast
        de franciscanen en clarissen) en worden daarom gewoonlijk tertiarissen en
        tertianen genoemd.  </p>

    <p class='indent'>De verhouding tussen
        actie en contemplatie kon ook bij deze religieuze leken sterk variëren. Behalve
        in de soberheid van hun kledij en schoeisel en de afzijdigheid van werelds
        vermaak, kwam de penitentie die hun leven bepaalde ook tot uiting in frequent
        kerkbezoek en het zo goed mogelijk volgen van het ritme van de dagelijkse zeven
        getijden: een contemplatieve trek in hun spiritualiteit. Andere aspecten van
        hun bestaan richtten zich daarentegen sterker op de buitenwereld, zoals het
        geven van aalmoezen en het beoefenen van deugden
        in de omgang met naasten.Voor sommige religieuze leken – maar lang niet voor
        alle – ging de ‘actieve’ kant van hun bestaan nog veel verder. In de snel
        groeiende steden waarin zij leefden werden, net zoals dat nu nog het geval is,
        de noden van de samenleving scherp gevoeld. Zo werden steeds meer gasthuizen
        opgericht om mensen op te vangen die niet meer voor zichzelf konden zorgen. De
        kloosters die vanouds aan hospitaalzorg hadden gedaan, konden de vraag daarnaar
        in de steden niet meer aan. Leken namen deze taak dus zelf ter hand. Het
        personeel dat aan zo’n hospitaal verbonden was, bestond nogal eens uit een staf
        van broeders en/of zusters die de lekenstatus hadden, maar leefden volgens een
        reglement dat was geïnspireerd door het kloosterleven. Begijnen en andere
        religieuze leken deden ook aan ziekenzorg thuis. Vanaf het midden van de
        veertiende eeuw werd Europa geteisterd door steeds nieuwe uitbraken van de
        pest. De zware taak van het verplegen van pestlijders werd aanvaard door nog
        weer een andere categorie van religieuze leken, die cellebroeders en – zusters
        genoemd zouden worden. Verpleeghulp ging hier als vanzelf over in ziekentroost,
        stervenshulp en uitvaartverzorging. </p>

    <p class='indent'>De houding van de
        Kerk tegenover dergelijke groeperingen was aan sterke schommelingen onderhevig.
        Tijdens het Concilie van Vienne (1311) overheerste de paniek over verdenkingen
        van ketterij, zodat er toen een rem op de ontplooiing van de religieuze leken
        werd gezet en zelfs vervolgingen losbarstten, met name van de begijnen. Maar
        rond 1400 was de kerkelijke leiding opmerkelijk tegemoetkomend, evenals
        trouwens wereldlijke overheden. Zo keurde de paus in korte tijd verschillende
        kapittels van tertiarissen goed. Daarvan was het Kapittel van Utrecht het
        omvangrijkst: het bestreek heel het middeleeuwse bisdom Utrecht, en de paus
        belastte dan ook de bisschop met de uitvoering van zijn privilege. In ons land
        hadden de tertiarissen meestal een contemplatieve leefwijze, want ze maakten
        deel uit van de brede hervormingsbeweging die als de ‘Moderne Devotie’ bekend
        staat. Maar bij anderen gaf de tertiarissenstatus vorm aan een spiritualiteit
        die – met instemming van het kerkelijk gezag – gericht was op hospitaalzorg en
        andere vormen van verpleging. </p>

    <p class='indent'> Als resultaat
        hiervan kwam een paradoxale en verwarrende ontwikkeling op gang die de hele
        vijftiende eeuw in beslag zou nemen. Het gaat om het proces van de zogenaamde
        ‘verkloostering’. Daarmee wordt bedoeld dat gemeenschappen van religieuze leken
        stap voor stap en ieder in eigen tempo opschoven in de richting van een
        volledig kloosterlijke leefwijze. Tertiarissen in ons land begonnen al vroeg de
        gelofte van kuisheid af te leggen. In de loop van de vijftiende eeuw kwamen
        daar ook de twee andere geloften bij, die van armoede en gehoorzaamheid. Een
        flink aantal tertiarissenconventen ging zelfs over tot de instelling van<span
            style='color:#CE181E'> </span>het kloosterslot. Deze trend werd aangemoedigd
        door de kerkleiding, die in overleg met het wereldlijk bestuur aan religieuze
        leken onderdelen van de religieuze status toekende, zoals vrijstelling van
        belastingen.  </p>

    <p class='indent'>Een volgende fase
        ging in toen sommige tertiarissen en tertianen met
        instemming van de bisschop overgingen naar een echte kloosterregel, in het
        bijzonder die van Augustinus: ze werden dus ‘regularissen’ of ‘regulieren’<span
            style='color:#CE181E'>, </span>voluit ‘reguliere kanunnikessen’ respectievelijk
        ‘reguliere kanunniken’<span style='color:#CE181E'>.</span> Rond 1450 begon het
        kerkelijk gezag bovendien aandrang uit te oefenen op gemeenschappen die nog
        helemaal geen regel volgden om de regel van Augustinus over te nemen, met enig
        succes. Maar ook na zo’n overstap bleven er in de leefwijze en spiritualiteit
        van dergelijke religieuzen trekken bewaard uit de tijd dat zij formeel nog
        leken waren. Vanouds hadden reguliere kanunniken en kanunnikessen het volledige
        Latijnse Officie gebeden, een taak die veel tijd, energie én scholing vereiste.
        Voor veel nieuwe zustergemeenschappen onder de regel van Augustinus viel daar
        niet aan te denken. </p>

    <p class='indent'>Ook al waren ze formeel nu
        kloosterling, ze bleven eenvoudige ‘lekenzusters’ of ‘conversinnen’, in de zin dat ze
        geen koorzusters waren. Dankzij de flexibiliteit van de regel van Augustinus
        kon dit allemaal worden gearrangeerd. Het waren vooral vrouwenconventen die
        hiervan profiteerden, maar er behoorden ook mannengemeenschappen toe. De
        cellebroeders oftewel alexianen namen de
        regel van Augustinus aan omdat deze hen toestond hun taak in de wereld –
        verpleging van pestlijders – uit te oefenen zonder gehinderd te worden door het
        koorgebed. </p>

    

    <p class='indent'>De zestiende eeuw maakte een
        einde aan de dynamische en fluïde laat-middeleeuwse situatie en bracht een kerkelijke
        regelgeving voort die het kloosterleven tot in de twintigste eeuw diepgaand
        heeft beïnvloed. De veranderingen vonden plaats in de tijdgeest die ook leidde
        tot de Reformatie en vormden een reactie op een zwakke religieuze moraal.
        Een eerste teken van verandering kwam met het
        decreet <i>Inter cetera </i>van paus Leo X (1521), dat de leden van de Derde
        Orde betrof, dus ook de talrijke tertiarissen in ons land. De drie geloften
        werden verplicht gesteld, de tertiarissen werden aangemoedigd om het volledig
        Officie te bidden, en zij die gekozen hadden voor het kloosterslot
        moesten zich daar ook echt aan houden. Wel
        bepaalde de paus nadrukkelijk dat niet-besloten kloosters hun taak in
        hospitaalzorg en <i>caritas</i> mochten voortzetten. Op deze manier werden de
        ontwikkelingen van de voorafgaande eeuw gesanctioneerd maar tegelijk ook
        gefixeerd. De paus knoopte tevens aan bij het </p>

    <p class='indent'>‘franciscaanse’ karakter van de
        Derde Orde om de leden ervan onder het gezag van de minderbroeders te stellen.
        Vier jaar tevoren waren de ‘observanten’ (strenge franciscanen) aangewezen als
        de officiële representant van de minderbroederorde.  Hun richting was in de
        vijftiende eeuw opgekomen en had geleidelijk en onder veel strijd de oudere
        richtingen binnen de familie van Franciscus overvleugeld. De in 1517 nieuw
        opgezette orde was streng gecentraliseerd en functioneerde als stoottroep van
        het pauselijk gezag. Veel tertiarissen verzetten zich tegen onderschikking aan
        de franciscanen, in ons land tot het einde (rond 1580) met succes. </p>

    <p class='indent'>Maar de verstrakking
        van het regime van de kloosterlingen werd vooral bevorderd door de
        Contrareformatie. In het verweer van de Kerk tegen de Hervorming van Luther
        moesten alle zeilen worden bijgezet. Op de kloosters, die door de hervormers
        zwaar onder vuur werden genomen, mocht niet het minste aan te merken zijn. In
        zijn vijfentwintigste en laatste zitting schreef het Concilie van Trente
        (1545-1563) aan alle vrouwelijke religieuzen het afleggen van plechtige
        geloften voor, evenals de onderhouding van het kloosterslot in een door de Kerk
        goedgekeurde orde. Dit decreet werd bekrachtigd door de bul <i>Circa Pastoralis
        </i>van paus Pius V (1566). Daarin onderstreepte hij de geldigheid van de bul <i>Periculoso
        </i>van 1298, die nu ook voor de tertiarissen van kracht werd verklaard. Alleen
        wie de drie geloften aflegde en het slot in acht nam, gold voortaan als
        ‘religieuze’. </p>

    <p class='indent'>Een deel van de
        tertiarissen had hier geen problemen mee, omdat deze bepalingen de richting
        onderstreepten waarin hun evolutie toch al was gegaan. Ze waren zelf vanouds
        sterk contemplatief ingesteld en ze hadden – zoals we zagen – uit eigen
        aandrang al eerder de drie geloften en het slot geaccepteerd. Dit deel van de
        tertiarissen zou in de zeventiende eeuw doorgaan op de ingeslagen
        contemplatieve weg. Dat gebeurde uiteraard in hoofdzaak buiten Nederland, waar
        de Opstand vrijwel een eind maakte aan het kloosterleven; alleen in grote delen
        van de huidige provincie Limburg en in enkele buitenlandse enclaves in
        NoordoostBrabant bleef kloosterleven mogelijk. Exemplarisch is het ontstaan in
        de Zuidelijke </p>

    <p class='indent'>Nederlanden (1623) van de
        Congregatie van Limburg van de penitenten-recollectinen, waarbij in het huidige
        Nederland de zusters aan de Beekpoort in Weert en een gemeenschap in Maastricht
        zich aansloten. Hun leven was er een van strenge versterving en van afkeer van
        de wereld. Ze plaatsten zich onder de leiding van de ‘recollecten’, een
        contemplatieve onderafdeling van de orde van de franciscanen. Maar in het
        eerste deel van hun naam komt tot uitdrukking dat ze zich nog steeds als
        tertiarissen beschouwden en in de traditie van de penitentieregel van 1289 (zie
        boven) wensten te staan. Pas in de negentiende eeuw zou de geschiedenis van
        deze gemeenschap een opmerkelijke ommezwaai maken, toen juist zij – als
        franciscanessen van Oirschot – het apostolaat ter hand gingen nemen. </p>

    <p class='indent'>Voor die tertiarissen
        echter van wie de leefwijze al in de zestiende eeuw actief was, betekenden de
        besluiten van Trente en de bul <i>Circa Pastoralis </i>een enorme stap terug.
        Nu had de kerkleiding toch wel waardering voor hun activiteiten. Hospitaalzorg,
        verpleging aan huis, zorg voor bijzondere behoeftigen zoals krankzinnigen - en
        inmiddels was daar ook het onderwijs bijgekomen - waren niet minder nodig dan
        voorheen, en de Kerk wilde dit alles niet belemmeren. Alleen wenste zij de
        beoefenaars van dergelijke <i>caritas</i>, voorzover het om vrouwen ging, niet
        als ‘religieuzen’ te erkennen: die titel bleef gereserveerd voor degenen die de
        plechtige geloften aflegden en het slot onderhielden.  </p>

    <p class='indent'>Het uitvloeisel van
        de beslissingen van de jaren 1560 was de totstandkoming van een duaal stelsel,
        dat sterk verschilde van de hiervoor geschetste situatie in de Late </p>

    <p class='indent'>Middeleeuwen. Het heeft wel de
        beeldvorming met terugwerkende kracht beïnvloed, mede doordat het systeem in
        grote trekken van kracht bleef in de negentiende en twintigste eeuw, de tijd
        waarin de ordegeschiedschrijving bloeide. Er werd een verschil geïntroduceerd
        tussen plechtige en eenvoudige geloften dat in de Middeleeuwen niet had
        bestaan. Plechtige geloften golden als eeuwigdurend en konden alleen worden
        afgelegd in goedgekeurde orden, dat wil zeggen orden die door de Kerk als
        zodanig waren erkend ten tijde van het Vierde Concilie van Lateranen of in de
        periode daarna als uitvloeisel van de spelregels die door dat concilie waren
        vastgesteld (zie boven). Voor vrouwen kwam daar de eis bij dat zij zich aan het
        strenge zogenaamde ‘pauselijke’ slot hielden, dat eveneens uitsluitend voor
        erkende orden gold. </p>

    <p class='indent'>Alleen diegenen die aan deze
        eisen voldeden golden als ‘religieuzen’.  </p>

    <p class='indent'>Daarnaast kwam er
        vanaf de zeventiende eeuw toch weer ruimte voor allerlei vormen van apostolaat,
        ook voor vrouwen. Maar zij kregen niet de status van ‘religieuzen’; in plaats
        daarvan moesten ze genoegen nemen met de rang van ‘zusters’. Ze legden eenvoudige
        geloften af die regelmatig hernieuwd moesten worden, waren óf helemaal niet aan
        het slot gebonden, óf aan het zogenaamde bisschoppelijke slot, en ze waren
        natuurlijk ook niet verplicht tot het koorgebed. Ze konden zich wel bovenlokaal
        organiseren, alleen waren het dan geen orden, maar ‘congregaties’. Een mooi
        voorbeeld zijn de Liefdezusters, die werden bijeengebracht door de Fransman
        Vincentius a Paulo en die zich het lot aantrokken van armen, zieken,
        verwaarloosden en vondelingen. Door de niet-religieuze status van zijn zusters
        te accepteren slaagde Vincentius er in 1634 in om goedkeuring te verkrijgen
        voor zijn beweging. </p>

    

    <p class='indent'>Na de bijna-onderbreking ten
        tijde van de Republiek (1588-1795) en de
        volledige afschaffing van de kloosters in de Franse tijd (1795-1814) kwam vanaf
        1814 het kloosterleven in Nederland weer op gang, eerst nog moeizaam en met
        veel tegenwind, daarna in een steeds hoger tempo. Aan de ontwikkelingen van de
        zeventiende en achttiende eeuw had het kloosterleven in ons land nauwelijks deel
        kunnen hebben. Het moest dus grotendeels opnieuw worden geïmporteerd en week
        daarmee sterk af van dat aan het eind van de Middeleeuwen. De twee
        belangijkste, onderling samenhangende, verschillen betroffen het intussen
        ontstane scherpere onderscheid tussen een actieve en een contemplatieve
        spiritualiteit en het duale stelsel met zijn onderscheid tussen echte
        ‘religieuzen’ en zusters met een mindere status. </p>

    <p class='indent'>Beschouwelijke en op
        apostolaat gerichte kloostergemeenschappen hadden in het Europa van de Nieuwe
        Tijd (1600-1800) naast elkaar bestaan. Maar met name in de achttiende eeuw was
        het gewicht verschoven in de richting van het actieve kloosterleven. De Kerk
        maakte ruim baan voor diegenen die zich vanuit godsdienstige bevlogenheid
        wilden inzetten voor het lenigen van de maatschappelijke noden. De bul <i>Quamvis
            iusto </i>van Benedictus XIV (1749) bracht een systematische regeling tot stand
        van de plek die de congregaties in de Kerk zouden innemen. Ze zouden pauselijke
        goedkeuring kunnen verkrijgen, maar bleven dan toch onder het gezag van de
        bisschop staan. En in de positie van de leden van die congregaties kwam
        verbetering: hoewel ze nog steeds niet tot de ‘religieuzen’ werden gerekend,
        zouden ze voortaan wel delen in de privileges van geestelijke personen. </p>

    <p class='indent'>Externe beïnvloeding
        bracht de ontwikkeling naar een sterker actief kloosterleven in een
        stroomversnelling. De Oostenrijkse keizer Jozef II, die ook de landsheer van de
        Zuidelijke Nederlanden was, achtte als verlicht vorst de overheid
        verantwoordelijk voor het welzijn van de samenleving, maar kende in de
        bevordering daarvan ook aan de Kerk een rol toe. Hij nam enerzijds maatregelen
        om de macht van de kerk in de samenleving tegen te gaan, zoals het verdrijven
        van de Jezuïeten. En nog voordat de Franse revolutionairen de kloosters
        helemaal afschaften, had Jozef II in 1783 al een eind gemaakt aan de
        contemplatieve instellingen in zijn gebied. Anderzijds was in zijn beleidsvisie
        wel plaats voor kloosters met een
        maatschappelijk nut, zoals zielzorg, onderwijs en verpleging. Toen na 1814 in
        ons land het kloosterleven weer werd geïmporteerd, was dat vooral in deze
        Zuidnederlandse vorm waarop het jozefinisme zijn stempel had gedrukt. Van
        1815-1830/9 waren Nederland en België staatkundig immers één geheel. </p>

    <p class='indent'>De religieuze
        gemeenschappen die na de Franse tijd in Nederland neerstreken behoorden dan ook
        allereerst tot de actieve categorie. Nederland was verarmd en ontredderd uit de
        Franse bezetting tevoorschijn gekomen, aan maatschappelijk dienstbetoon was
        veel behoefte. En nu bleek het kerkrechtelijk-organisatorische onderscheid
        tussen ‘religieuzen’ (echte kloosterlingen) en eenvoudige zusters een
        onverwacht voordeel op te leveren. De revolutionaire tussenperiode had
        weliswaar scheiding van Kerk en Staat en vrijheid van godsdienst voor de
        katholieken gebracht, maar daarmee was het herstel van de katholieke
        instellingen nog niet geregeld. Koning Willem I zette het Franse beleid voort
        van het volstrekt weren van alle kloosters. Maar de eenvoudige zusters die aan
        het werk werden gezet in verzorging, verpleging en onderwijs zagen er niet uit
        als echte kloosterlingen en konden aan de aandacht ontsnappen. Ze gingen
        samenwonen als ‘wereldlijke vereniging’ en slaagden erin
        om in die vorm toegelaten te worden.  </p>

    <p class='indent'>Tot de eerste
        gemeenschappen die zich zo in de negentiende eeuw aan het apostolaat wijdden,
        behoorden de franciscanessen van Dongen en die van Oirschot. ‘Franciscanessen’
        behoorden tot de Derde Orde van Franciscus. Het lijkt dus alsof deze twee
        gemeenschappen het stokje overnamen van de laatmiddeleeuwse tertiarissen, maar
        zo eenvoudig is het niet. De gemeenschap in Dongen werd in 1801 gesticht door
        vier uit de Zuidelijke Nederlanden gevluchte zusters, twee
        penitenten-recollectinen en twee ‘augustinessen’ (ook deze term wordt exclusief
        gebruikt voor de negentiende en twintigste eeuw; bedoeld zijn zusters die leven
        volgens de regel van Augustinus en de traditie voortzetten van de reguliere
        kanunnikessen). De gemeenschap in Oirschot kwam voort uit die van de zusters
        aan de Beekpoort in Weert, en ook zij waren penitenten-recollectinen. Voor
        beide gemeenschappen en de eruit voortgekomen congregaties is
        ‘penitenten-recollectinen’ dan ook nog lang de officiële aanduiding geweest.
        Maar in het voorgaande is al aan de orde gesteld dat zij bij uitstek
        contemplatief waren ingesteld. Er was dus aandrang van de plaatselijke pastoors
        voor nodig om de zusters over te halen hun beschouwelijke leefwijze te matigen
        en ruimte te maken voor apostolaat. Niet toevallig was het onderwijs de eerste
        taak die zij ter hand namen: als slotzusters hadden de penitenten-recollectinen
        wel ervaring kunnen opdoen met meisjesinternaten. Vooral bij de franciscanessen
        van Oirschot is de spanning tussen actie en contemplatie altijd voelbaar gebleven.
    </p>

    <p class='indent'>In Tilburg haalde
        pastoor Zwijsen – de latere aartsbisschop van Utrecht -  een zestal vrouwen
        naar zijn stad om maatschappelijk werk te doen onder de armsten. Ze legden in
        zijn handen de geloften af, en zo ontstond de eerste vestiging van de Zusters van
        Liefde van Tilburg, die zou uitgroeien tot de grootste actieve congregatie in
        ons land. Ook dit initiatief was lokaal, maar de inspiratie kwam duidelijk uit
        de internationale beweging van de Liefdezusters van Vincentius a Paulo. </p>

    <p class='indent'>Zo werd in de eerste helft
        van de negentiende eeuw een start gemaakt met de herplanting van het
        kloosterleven in Nederland. Maar door allerlei tegenwerking en ongunst van de
        tijden ging het eerst langzaam. Vleugels kreeg het kloosterleven pas door
        enkele keerpunten in het midden van de eeuw. In 1840 besloot de nieuwe koning
        Willem II dat een aantal bestaande kloosters die bijna waren uitgestorven weer
        nieuwe leden mochten aannemen en in 1848 werd een nieuwe grondwet afgekondigd,
        die aan het katholieke volksdeel </p>

    <p class='indent'>volledige vrijheid verleende om
        het godsdienstig leven naar eigen wens in te richten. Dit had tot gevolg dat in
        1853 de kerkelijke hiërarchie kon worden hersteld en nieuwe bisdommen
        opgericht.<b> </b>In de halve eeuw die volgde organiseerden de katholieken
        zich, parallel aan de orthodoxe protestanten, tot een eigen zuil, en aan die
        ontwikkeling namen de kloosters volop deel. </p>

    <p class='indent'>Het is vooral in deze
        periode dat talrijke congregaties tot stand kwamen. Veelal was het de  lokale
        geestelijkheid die een eigen congregatie wenste om de zorg op zich te nemen
        voor onderwijs en ziekenzorg in hun gebied.<b> </b>Vanuit een moederklooster
        werden in andere steden en dorpen dochterinstellingen (succursalen) opgericht.
        Het bestuur van een dergelijke groep bleef gecentraliseerd en berustte bij de
        overste van het moederhuis. Zo’n congregatie heette dan ook naar het hoofdhuis:
        franciscanessen van Bennebroek, dominicanessen van Voorschoten.
        Voor de vele gevallen waarin een congregatie zich uitbreidde naar andere
        bisdommen dan die waar het oorspronkelijke lokale intiatief had gelegen,
        moesten bijzondere gezagsregelingen worden ontworpen. Soms leidde de stichting
        van een nieuwe vestiging tot een afsplitsing en het ontstaan van een nieuwe
        congregatie. </p>

    <p class='indent'>Vanaf 1870 werd het
        Nederlandse kloosterleven verrijkt door de komst van buitenlandse congregaties.
        Die hadden gewoonlijk al wel kerkelijke erkenning gekregen, maar raakten in de
        problemen door het beleid van wereldlijke overheden. Eerst waren het vooral
        religieuze gemeenschappen uit Duitsland die in ons land hun toevlucht namen
        voor de ‘Kulturkampf’ die Bismarck tegen de katholieke kerk had ontketend. Rond
        1900 kwamen er in twee golven kloosterlingen vanuit Frankrijk naar Nederland,
        verjaagd door de Franse politiek van ‘laicité’ (lekenkarakter van de samenleving).
        Langs deze weg vestigden zich onder meer de benedictijnen, na eeuwenlange
        afwezigheid, opnieuw in ons land. </p>

    <p class='indent'>In de tweede helft
        van de negentiende eeuw werd ook het Nederlandse koloniale rijk in Oost- en
        West-Indië werkterrein voor Nederlandse kloosterlingen. Naast de <i>caritas </i>en
        het onderwijs gingen kloosterlingen zich bezighouden met de missie. Naar
        Oost-Indië vertrokken jezuïeten, naar West-Indië dominicanen (Nederlandse
        Antillen) en redemptoristen (Suriname). Een aantal internationale
        missiecongregaties begonnen vanaf 1880 ook in Nederland met een opleidingshuis,
        zoals de Missionarissen van het H. Hart, de Witte Paters, de Missionarissen van
        Mill Hill en de Sociëteit voor Afrikaanse Missiën. De
        eigenlijke verkondiging was uiteraard voorbehouden aan congregaties van
        missiepaters, zoals de Missionarissen van de Heilige Familie. Maar in allerlei
        ondersteunende functies – ook die van het gebed! - konden vrouwelijke
        religieuzen worden ingezet, van wie de organisaties vaak verbonden waren met
        die van hun mannelijke tegenhangers, bijvoorbeeld de Missie- en
        Aanbiddingszusters van de Heilige Familie.  </p>

    

    <p class='indent'>Dit stuk eindigt met een kort
        overzicht van de ontwikkelingen in de twintigste eeuw. De bul <i>Conditae a
            Christo </i>van paus Leo XIII (1900), die werd uitgewerkt in de <i>Normae </i>van
        1901, bracht een nadere afbakening van het gezag van de bisschop, de paus en de
        oversten ten opzichte van de congregaties. Hiermee werd gepreludeerd op de
        nieuwe kerkelijke wetgeving, vervat in de <i>Codex Iuris Canonici </i>die in
        1917 van kracht werd. Dit wetboek onderscheidde twee soorten ‘religieuzen’:
        kloosterlingen met eenvoudige en met plechtige geloften. Van de verschillende
        soorten geloften werden precieze definities gegeven. De plechtige geloften
        werden afgelegd in orden, de eenvoudige in congregaties. Naast publieke waren er private geloften. Plechtige geloften
        waren per definitie eeuwig, eenvoudige konden zowel tijdelijk als eeuwig zijn.
        Maar belangrijker dan deze verschillen was dat de religieuzen met eenvoudige<span
            style='color:#CE181E'> </span>geloften nu ook als volwaardige ‘religieuzen’
        werden erkend.  </p>

    <p class='indent'>Zoals op alle
        kerkelijke terreinen betekende het Tweede Vaticaans Concilie (1962-65) ook voor
        het kloosterleven een ingrijpende herbezinning. ‘Aggiornamento’ (aanpassing aan
        de dag van vandaag) was
        het door paus Johannes XXIII gegeven parool. De manier waarop het kloosterleven
        bij de tijd moest worden gebracht werd nader uiteengezet in het decreet <i>Perfectae
            Caritatis </i>(1966). Herbezinning op de oorspronkelijke intenties van de
        ordestichters en op de betekenis van de oude regels werd aangeprezen als de
        methode. Actieve religieuzen werden aangespoord tot radicale openheid naar de
        wereld. Deze wending luidde voor alle kloosterlingen een periode in van
        intensief zoeken naar een nieuwe zingeving aan hun religieus bestaan. </p>

    <p class='indent'>Een nieuw kerkelijk
        wetboek werd geïntroduceerd in 1983. Dit hanteert het onderscheid tussen
        plechtige en eenvoudige geloften niet meer om verschil te maken tussen orden en
        congregaties: ze worden nu samengevat onder de algemene categorie van de
        ‘religieuze instituten’. Op dat moment tekende zich de neergaande lijn in het
        kloosterleven al af. Terugloop van de roepingen, vergrijzing en het sluiten van
        talrijke kloosters vormen een actualiteit die op dit moment van schrijven nog
        niet is afgesloten. </p>

    

