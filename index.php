    <p class="indent">Voor de aanduiding van de orden waartoe de kloosters behoorden is de gangbare terminologie
        gebruikt. In de literatuur komt men nogal eens vrouwenconventen ‘van de derde orde van Augustinus’ tegen. In
        Goudriaan (2008) wordt betoogd dat een dergelijke orde in ons land in de Middeleeuwen niet heeft bestaan. Het
        gaat om ‘low budget’ vrouwenkloosters die de (standaard-)regel van Augustinus volgden, maar waar alleen
        conversinnen (lekenzusters) aanwezig waren en koorzusters ontbraken. Als overkoepelende benaming is in
        dergelijke gevallen de term ‘regularissen’ gebruikt. Waar bekend is dat een klooster alleen door conversinnen
        werd bewoond, is dat in het veld Stadia vermeld: zie bijvoorbeeld G20.</p>

