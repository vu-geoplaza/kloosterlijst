    de gegevens slaan op het klooster Bethlehem/Bangert onder Westerblokker.</p>



    <p class="indent"><strong>Westerburen: Muller, <i>Gesch. Atlas</i> I,
    536</strong>. Het gaat om een uithof van Veenklooster (O24).</p>



    <p class="indent"><strong>Westerwird (tussen Beers en Jorwerd),
    norbertinessen: Muller, <i>Gesch. Atlas </i>I, 567; <i>Mon. Bat. </i>II 206 en
    wikipedia (7 juni 2008) art. ‘Kloosters in Friesland’. </strong>Uithof van Lidlum?</p>



    <p class="indent"><strong>Wijk, tempeliers: <i>Mon. Bat. </i>II 211
    nr. 2.</strong> Zie Zierikzee.</p>



    <p class="indent"><strong>Wijk-bij-Duurstede, regularissen: Muller, <i>Gesch.
    Atlas</i> I, 385</strong>. Muller baseert zich op De la Torre, <i>AAU
    </i> 10 (1882) 192, die van twee vrouwenkloosters in Wijk-bij-Duurstede rept.
    Maar De la Torre is zoals wel vaker slecht geïnformeerd. Zie W23.</p>



    <p class="indent"><strong>Woerden, Barbaraklooster in de Voorstraat:
    Muller, <i>Gesch. Atlas</i> I, 393</strong>. Als bron noemt
    Muller de <i>Tegenw. Staat</i>, maar die vermeldt VIII, 14 alleen het
    Barbaragasthuis voor vreemde wezen en behoeftige oude mannen en vrouwen. De
    kaart van Van Deventer kent geen kloosters in Woerden; ook Plomp (1972)
    vermeldt er geen.</p>



    <p class="indent"><strong>Woerden, klooster in de Warmoesstraat:
    Muller, <i>Gesch. Atlas</i> I, 393, op grond van Van
    der Aa XII, 548. </strong>Hiervoor geldt hetzelfde als
    voor het Barbaraklooster.</em></p>


    <p class="indent"><strong>Wognum, regularissen van Sint-Ursula: <i>Mon.
    Bat. </i>II 210. A.F. Ranshuysen signaleert in </strong><i>Bijdragen Bisdom Haarlem </i>1 (1873) 320 een
    ‘huis genaamd de 11000’ en vermoedt dat dit op een Ursulaklooster slaat. Maar
    van zo’n klooster is geen spoor te bekennen.</p>



    <p class="indent"><strong>Worcum, begijnen: Nübel (1970) 112. </strong>Gezien de context zal Nübel gedoeld hebben op de Hollandse stad
    Woudrichem, niet op het Friese Workum. Dan slaat de notitie op de tertiarissen
    van Sint-Dominicus (W30). Zie hierboven Alkmaar.</p>



    <p class="indent"><strong>Workum, dominicanessen, volgens <i>Desolata
    Batavia Dominicana </i>53. </strong>Verwarring
    met Woudrichem: Wolfs (1988) 114.</p>



    <p class="indent"><strong>Workum, kartuizers van Sint Ursula te
    Heidenschap: wikipedia (7 juni 2008) art. ‘Kloosters in Friesland’. </strong>Deze vermelding van een kartuizerklooster
    bij Workum gaat vermoedelijk terug op Van Buijtenen, <i>De Vrije Fries </i>36
    (1941) 134-136. Reeds verworpen door Schoengen, <i>Mon. Bat. </i>III 132; ook geen
    vermelding in Gruijs (1975). Voor het Sint-Ursula-patrocinium in Workum zie
    volgend lemma.</p>



    <p class="indent"><strong>Workum, tertiarissen Sint Ursula: Muller, <i>Gesch.
    Atlas </i>I, 591; <i>Mon. Bat. </i>I 203 nr. 1</strong>. Het Ursulapatrocinium berust op een misverstand:
    vriendelijke mededeling van J.A. Mol bij brief van 30 september 1997.</p>



    <p class="indent"><strong>Wulpen, norbertijnen: <i>Mon. Bat. </i>II,
    211. </strong>Van een
    norbertijnenklooster op het eiland Wulpen is geen spoor te bekenen: zie
    Gottschalk (1983).</p>



    <p class="indent"><a name="z"></a><strong>Zaamslag, tempeliers: Muller, <i>Gesch.
    Atlas </i>I, 433; <i>Mon. Bat. </i>II 213. </strong>Ook Gottschalk (1984) 42 spreekt nog van een
    commanderij van de tempeliers ten westen van Zaamslag, maar erkent op p. 90 dat
    zij niets over deze tempeliers gevonden heeft. Recentelijk heeft R. Lensen in
    een publicatie in eigen beheer nog weer een pleidooi gehouden voor de erkenning
    van een commanderij van de tempeliers in Zaamslag. Hij beroept zich onder meer
    op een oorkonde uit 1288 ten behoeve van de Tempel te Gent, die zich in het
    archief van het bisdom Doornik bevindt. Hierin is sprake is van goederen ‘apud
    Zaemslacht, domum militiae Templi’. De tekst is onduidelijk. Zou het om een
    zelfstandige commanderij gaan, dan is het merkwaardig dat er na 1312 geen
    aanwijzingen zijn voor de aanwezigheid van johannieters te Zaamslag (het
    hospitaal te Zaamslag, zie Z37, had een andere oorsprong). Daarom verdient de
    opvatting van De Kraker (1994) en (1997) 264, dat het om een uithof van de
    tempelieren te Gent gaat, de voorkeur. Voor de tempelierenlegende zie
    Zierikzee.</p>



    <p class="indent"><strong>Zaltbommel, karmelieten: <i>Mon. Bat. </i>III,
    137; nog gevolgd door Coomans (2002) noot 24. </strong>Uit het bij Knippenbergh (1719) 121-122
    weergegeven testament (1450) van ridder Johannes van Broekhuizen (op pelgrimage
    overleden 1452*) blijkt dat het zijn bedoeling was dat in Zaltbommel een
    karmelietenklooster werd gesticht. Maar uit de bij Brom<i> </i>I (1908) 1 nr.
    174 weergegeven bulle d.d. 18 jan. 1459, die toestemming geeft dit klooster in
    Goch of elders te vestigen, blijkt dat de stichting in Zaltbommel niet is
    doorgegaan. Hiermee is de weergave van de feiten door Schoengen enigszins
    gecorrigeerd. Van een overdracht van goederen aan Pieterswiel is aan de auteurs
    van het <i>Mon. Wind. </i>III, 513-524 niets bekend. Later was er in Zaltbommel
    wel een terminarishuis van de karmelieten: zie behalve Schoengen ook Maris
    (1939); Reinders (1977) 29; 41; en Henderikx (1977) 43.</p>



    <p class="indent"><strong>Zierikzee, johannieters: <i>Mon. Bat. </i>II
    216 nr. 7: ‘Sion / Noordgouwe’</strong>. Zie Van Beresteyn
    (1934) 53; Henderikx (1977) 106: een johannieter commanderij in Zierikzee is
    niet bekend. Wat de aanduiding Sion / Noordgouwe betreft: blijkbaar is er
    verwarring met het kartuizer klooster aldaar.</p>



    <p class="indent"><strong>Zierikzee, tempeliers: <i>Mon. Bat. </i>II
    215 nr. 6. </strong>Zie Wolfs (1973); Henderikx (1977) 106;
    Streefland (1979-80). Berichten over tempeliers te Zierikzee en op veel andere
    plaatsen weerspiegelen de tempelierenlegende<strong>. </strong>De geforceerde opheffing
    van de tempelierenorde heeft sterk tot de verbeelding gesproken. Ook de
    overdracht van een deel van de goederen van deze orde aan de johannieters heeft
    de overlevering beïnvloed. Enkele andere opgeheven orden, met name de
    zakbroeders, de eksterbroeders en de lazarieten, zijn in de herinnering
    vervangen door de tempeliers. In veel gevallen werden onverklaarde muurresten
    e.d. als ‘tempelierenklooster’ geduid. In Zierikzee ligt de overdracht van het
    huis van de in 1274 opgeheven orde der eksterbroeders aan de predikheren
    (oorkonde van 23 februari 1286; <i>OSU </i>IV, 2263) ten grondslag aan het
    ontstaan van de tempelierenlegende.</p>



    <p class="indent"><strong>Zierikzee, Duitse Orde: <i>Mon. Bat.</i> II 216 nr. 8 en vgl. 215 nr. 3. </strong>Het enige houvast is de overdracht in 1286* van
    het huis van de opgeheven orde der <i>fratres beate Marie </i>(<i>OSU </i>IV,
    2263). Maar het gaat hier om de eksterbroeders: Wolfs (1973). Zie verder
    Zierikzee, tempeliers.</p>



    <p class="indent"><strong>Zierikzee, karmelieten: Muller, <i>Gesch.
    Atlas </i>I, 205</strong>, op grond van <i>Zelandia Illustrat </i>I,
    427. Het gaat om het termijnhuis van de karmelieten van Mechelen, zie Henderikx
    (1977) 43.</p>



    <p class="indent"><strong>Zierikzee, augustijner eremieten: Muller, <i>Gesch.
    Atlas</i> I, 205</strong>. Dit bericht gaat terug op De la
    Torre, <i>AAU</i> 10 (1882) 175. Aan Henderikx (1977) is zelfs geen termijn
    huis van de augustijnen in Zierikzee bekend. Misschien een vage reminiscentie
    aan de eksterbroeders (Z10).</p>



