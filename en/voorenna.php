    <p class='indent'>
        In 2015 ging de samenwerking tussen de VU en het ENK van
        start. Op dat moment hadden de VU Kloosterlijst (voor de kloosters uit
        Middeleeuwen) en het deelbestand van ENK (voor de kloosters na 1800) al een
        vaste vorm gekregen. De afspraak werd gemaakt dat bij de uitbreiding van het
        ENK-naslagwerk met de kloosters tot aan 1800 het format hetzelfde zou zijn als
        dat voor de jongere kloosters en dat ook de VU-kloosterlijst zelfstandig zou
        blijven. Inhoudelijk zouden de beschrijvingen zoveel mogelijk op elkaar worden
        afgestemd. De basislijst van opgenomen kloosters is in het ENK-naslagwerk en in
        de Kloosterlijst identiek, en datzelfde geldt voor die van de middeleeuwse
        kapittels, van de uithoven (met hoven en refugia) en van de termijnhuizen. Aan
        het harmoniseren van de twee versies is in de jaren 2015-2019 veel tijd en
        energie besteed; ongetwijfeld zijn niet alle kleine oneffenheden weggewerkt. </p>

